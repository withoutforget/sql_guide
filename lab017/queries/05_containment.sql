-- Задача 5. Найти документы по ОБРАЗЦУ оператором содержания @> (только jsonb).
SET search_path TO lab017;

-- doc @> образец  ⇔  «левый документ СОДЕРЖИТ правый». Для объектов: все пары
-- образца есть в документе (на любой глубине). Идеально для фильтрации по
-- шаблону. Здесь — все товары бренда Sony:
SELECT id, name, attributes ->> 'brand' AS brand
FROM products
WHERE attributes @> '{"brand":"Sony"}'   -- образец-объект
ORDER BY id;

-- Для МАССИВОВ @> означает «каждый элемент правого массива содержится в
-- каком-то элементе левого». Так ищем заказы, где среди позиций есть
-- конкретный товар: правый образец — массив из одного НЕполного объекта
-- {"product": ...}, и он «вкладывается» в полную позицию {product, qty, price}.
SELECT id, customer
FROM orders
WHERE items @> '[{"product":"Наушники TWS Pro"}]'
ORDER BY id;
