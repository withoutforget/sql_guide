-- Задача 4. Выровнять числовой артикул нулями до фиксированной ширины (lpad).
SET search_path TO lab015;

-- lpad(строка, длина, чем) дополняет слева до нужной ширины. Классика —
-- добить число нулями: lpad('7', 4, '0') → '0007'. Так собирают аккуратные
-- коды фиксированной длины, которые ещё и правильно сортируются как текст.
SELECT
    id,
    name,
    art_no,
    'ART-' || lpad(art_no::text, 4, '0') AS code
FROM products
ORDER BY art_no;
