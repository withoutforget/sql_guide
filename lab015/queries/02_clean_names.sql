-- Задача 2. Очистка ФИО: убрать лишние пробелы по краям и внутри, привести регистр.
SET search_path TO lab015;

-- Цепочка очистки «грязной» строки:
--   btrim          — срезает пробелы по КРАЯМ;
--   regexp_replace — схлопывает внутренние повторы пробелов (\s+) в один
--                    (btrim внутренние пробелы не трогает — это его граница);
--   initcap        — каждое слово с заглавной, остальное строчными.
SELECT
    id,
    raw_name                                                        AS было,
    initcap(regexp_replace(btrim(raw_name), '\s+', ' ', 'g'))       AS стало
FROM clients
ORDER BY id;
