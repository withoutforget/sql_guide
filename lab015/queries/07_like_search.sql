-- Задача 7. Поиск по образцу: префикс артикула, подстрока без регистра, литеральный «%».
SET search_path TO lab015;

-- Три разных сопоставления с образцом на одной выборке:
--   LIKE 'AB%'   — префикс (регистр важен: 'ab-99' НЕ подойдёт);
--   ILIKE '%usb%'— подстрока без учёта регистра (поймает 'Мышь USB');
--   LIKE '%\%'   — строка, ЗАКАНЧИВАЮЩАЯСЯ буквальным знаком '%' (экранирование '\%').
SELECT
    sku,
    name,
    label,
    sku   LIKE 'AB%'   AS sku_ab_prefix,   -- начинается на 'AB' (в верхнем регистре)
    name  ILIKE '%usb%' AS name_has_usb,    -- содержит 'usb'/'USB' — регистр не важен
    label LIKE '%\%'   AS label_has_percent -- метка со знаком процента на конце
FROM products
ORDER BY id;
