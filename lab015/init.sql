-- ============================================================================
-- lab015 — Строки и регулярные выражения. Схема и тестовые данные.
-- Доменная область: «сырой» импорт CRM/каталога интернет-магазина —
-- грязные текстовые данные, которые нужно чистить, парсить и валидировать.
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
--
-- Данные подобраны так, чтобы ДЕМОНСТРИРОВАТЬ тему:
--   • clients.raw_name — ФИО с лишними пробелами и в разном регистре, часть без
--     отчества/имени → очистка (trim/initcap) и разбор на инициалы;
--   • clients.phone    — телефоны в 5 форматах + один NULL → нормализация к
--     единому виду +7XXXXXXXXXX и NULL-безопасная сборка строки;
--   • clients.email    — валидные и НЕвалидные адреса → проверка regex-валидатором
--     и извлечение домена (запрос не должен падать на битых);
--   • clients.address  — «город, улица, дом» через ', ' → разбор split_part;
--   • products.sku     — артикулы, часть в формате AB-1234, часть «битые» →
--     валидация регуляркой и поиск по префиксу (LIKE);
--   • products.art_no  — числовой артикул → выравнивание нулями (lpad → ART-0042);
--   • products.tags    — теги через запятую → разбиение и подсчёт;
--   • products.label   — метки, часть со знаком «%» → LIKE с ESCAPE;
--   • products.note    — свободный текст с числами → извлечение одного/всех чисел;
--   • products.name    — кириллица + латиница → length (символы) vs octet_length (байты);
--   • logs.line        — строки вида «key=value» → мини-ETL: парсинг + агрегация;
--   • companies.name   — одна и та же фирма, записанная по-разному → канонизация
--     и поиск дубликатов, «на глаз» разных, а по факту одинаковых.
-- ============================================================================

DROP SCHEMA IF EXISTS lab015 CASCADE;
CREATE SCHEMA lab015;
SET search_path TO lab015;

-- ── Клиенты: «сырые» карточки из импорта CRM ────────────────────────────────
CREATE TABLE clients (
    id        INT  PRIMARY KEY,
    raw_name  TEXT NOT NULL,   -- ФИО «как ввели»: регистр вразнобой, лишние пробелы, часть без отчества
    phone     TEXT,            -- телефон в произвольном формате; NULL = телефона нет
    email     TEXT,            -- e-mail: часть валидна, часть — нет
    address   TEXT NOT NULL    -- адрес «город, улица, дом» через запятую с пробелом
);

