-- ── Товары: каталог с артикулами, тегами, метками и свободным описанием ──────
CREATE TABLE products (
    id      INT  PRIMARY KEY,
    sku     TEXT NOT NULL,   -- внешний артикул: часть в формате AB-1234, часть «битая»
    name    TEXT NOT NULL,   -- название (кириллица + латиница) — для length vs octet_length
    art_no  INT  NOT NULL,   -- внутренний числовой артикул — для выравнивания нулями
    tags    TEXT NOT NULL,   -- теги через запятую (для разбиения)
    label   TEXT NOT NULL,   -- маркетинговая метка; часть содержит знак «%»
    note    TEXT NOT NULL    -- свободное описание с числами (для извлечения чисел)
);

