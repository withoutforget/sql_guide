-- Задача 5. Сколько заказов в июне пришлось на выходные, а сколько на будни (isodow).
SET search_path TO lab014;
SET TIME ZONE 'UTC';   -- детерминизм: день недели зависит от пояса, фиксируем UTC

-- isodow нумерует дни недели 1=пн..7=вс, поэтому «выходной» = isodow IN (6,7)
-- (суббота и воскресенье идут подряд — меньше шансов ошибиться, чем с dow, где
-- воскресенье = 0). Считаем июньские заказы двумя срезами через FILTER (lab001).
-- В июне 2024: заказы в субботы (1, 8, 15, 22 июня) — 4 шт. на выходных, остальные
-- 4 (3 июня ×2, 12, 28) — в будни.
SELECT
    count(*)                                                    AS всего_в_июне,
    count(*) FILTER (WHERE EXTRACT(isodow FROM created_at) IN (6,7)) AS в_выходные,
    count(*) FILTER (WHERE EXTRACT(isodow FROM created_at) IN (1,2,3,4,5)) AS в_будни
FROM orders
WHERE created_at >= '2024-06-01'
  AND created_at <  '2024-07-01';
