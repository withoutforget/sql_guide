-- Задача 4. Заказы III квартала 2024 (июль-сентябрь) через полуоткрытый интервал.
SET search_path TO lab014;
SET TIME ZONE 'UTC';   -- детерминизм: сравниваем момент в UTC

-- Правильный способ отобрать «всё за период» для timestamp/timestamptz —
-- ПОЛУОТКРЫТЫЙ интервал: нижняя граница включена (>=), верхняя исключена (<),
-- а верхняя = начало СЛЕДУЮЩЕГО периода. III квартал 2024 = [2024-07-01, 2024-10-01).
-- Так ловим любое время суток последнего дня квартала (30 сентября 23:59 — тоже
-- внутри). BETWEEN здесь ОПАСЕН: BETWEEN ... AND '2024-09-30' взял бы только
-- полночь 30-го и потерял почти целые сутки (см. theory/06). В квартал попадают
-- заказы июля (9,10,11) и сентября (12,13); августовских у нас нет.
SELECT
    id,
    created_at,
    amount
FROM orders
WHERE created_at >= '2024-07-01'
  AND created_at <  '2024-10-01'
ORDER BY created_at;
