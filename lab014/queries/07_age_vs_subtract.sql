-- Задача 7. Стаж клиента на 2024-09-30: age() (годы/месяцы/дни) против простого вычитания (дни).
SET search_path TO lab014;
SET TIME ZONE 'UTC';   -- детерминизм

-- Считаем «сколько клиент с нами» на ФИКСИРОВАННУЮ дату 2024-09-30 (не now() — иначе
-- вывод «поплыл» бы, см. theory/03). Два способа, чтобы увидеть разницу форматов:
--   age(a, b)   → «человеческий» интервал в годах/месяцах/днях (для показа людям);
--   a - b       → для date это просто ЧИСЛО ДНЕЙ (для сравнения/сортировки/усреднения).
-- registered — тип date; для age приводим к timestamp, чтобы получить интервал.
-- Сортируем по числу дней убыв. → самые «старые» клиенты сверху.
SELECT
    name,
    registered,
    age(timestamp '2024-09-30', registered::timestamp) AS стаж_чел,
    date '2024-09-30' - registered                     AS стаж_дней
FROM customers
ORDER BY date '2024-09-30' - registered DESC;
