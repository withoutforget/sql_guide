-- Задача 12. Профиль активности 2024-06-15 по 15-минутным корзинам (date_bin).
SET search_path TO lab014;
SET TIME ZONE 'UTC';   -- детерминизм: границы корзин — timestamptz, читаем в UTC

-- date_trunc умеет только «стандартные» периоды (час, день, месяц). Корзины СВОЕЙ
-- ширины (15 минут) строит date_bin(ширина, момент, начало_отсчёта): раскладывает
-- ось времени на равные интервалы от начала_отсчёта (полночь 15 июня) и возвращает
-- ЛЕВУЮ границу корзины, куда попал момент. Группируем события по корзине и считаем.
-- Корзины полуоткрытые [начало, начало+15м): напр. события 10:00/10:05/10:12 → все
-- в корзину 10:00 (счёт 3), а 10:50/10:58 → в корзину 10:45 (счёт 2).
SELECT
    date_bin(interval '15 min', event_at, timestamptz '2024-06-15 00:00:00+00') AS корзина,
    count(*)                                                                     AS событий
FROM events
GROUP BY date_bin(interval '15 min', event_at, timestamptz '2024-06-15 00:00:00+00')
ORDER BY корзина;
