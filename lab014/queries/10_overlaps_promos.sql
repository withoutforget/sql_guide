-- Задача 10. Какие промо-акции пересекаются по датам (оператор OVERLAPS).
SET search_path TO lab014;
SET TIME ZONE 'UTC';   -- детерминизм

-- (начало1, конец1) OVERLAPS (начало2, конец2) → true, если периоды имеют общее
-- время. Границы полуоткрытые: если периоды лишь КАСАЮТСЯ концами — это не
-- пересечение. Ищем пары через self-join с условием p1.id < p2.id (каждую пару
-- по одному разу, без самосравнения). Пересекается только «Летняя распродажа»
-- (01-15 июня) и «День рождения магазина» (10-20 июня) — общие дни 10-15 июня.
-- «Осенний сезон» и «Чёрная пятница» не пересекаются ни с кем.
SELECT
    p1.name                              AS акция_1,
    p1.starts_on || '..' || p1.ends_on   AS период_1,
    p2.name                              AS акция_2,
    p2.starts_on || '..' || p2.ends_on   AS период_2
FROM promotions AS p1
JOIN promotions AS p2 ON p1.id < p2.id
WHERE (p1.starts_on, p1.ends_on) OVERLAPS (p2.starts_on, p2.ends_on)
ORDER BY p1.id, p2.id;
