-- Фиксируем таймзону сессии, накатывающей данные. Это влияет на то, как будут
-- истолкованы timestamptz-литералы БЕЗ явного смещения. Мы на всякий случай
-- пишем ВСЕ timestamptz со смещением '+00' (UTC) явно — тогда результат вообще
-- не зависит от окружения. Файлы queries/*.sql, где важен вывод момента времени,
-- тоже ставят себе `SET TIME ZONE 'UTC';` (каждый psql-запуск — отдельная сессия
-- и НЕ наследует этот SET). См. theory/01.
SET TIME ZONE 'UTC';

-- ── Клиенты: даты регистрации разбросаны по годам (для age / арифметики) ──────
CREATE TABLE customers (
    id          INT PRIMARY KEY,
    name        TEXT NOT NULL,
    city        TEXT NOT NULL,
    registered  DATE NOT NULL          -- дата регистрации (тип date, без времени)
);

-- ── Заказы: момент оформления и доставки — timestamptz (момент времени в UTC) ─
--    amount — сумма заказа в рублях; delivered_at может быть NULL (ещё едет).
CREATE TABLE orders (
    id            INT PRIMARY KEY,
    customer_id   INT NOT NULL REFERENCES customers(id),
    amount        NUMERIC(10,2) NOT NULL,   -- сумма заказа, ₽
    created_at    TIMESTAMPTZ   NOT NULL,   -- когда оформлен (момент времени)
    delivered_at  TIMESTAMPTZ               -- когда доставлен; NULL = ещё не доставлен
);

-- ── События активности: для сессий, бакетирования по 15 минут ────────────────
CREATE TABLE events (
    id           INT PRIMARY KEY,
    customer_id  INT NOT NULL REFERENCES customers(id),
    event_at     TIMESTAMPTZ NOT NULL,      -- момент события
    kind         TEXT NOT NULL              -- 'view' | 'click' | 'cart' | 'purchase'
);

-- ── Промо-периоды: границы как date (для OVERLAPS и попадания в период) ───────
CREATE TABLE promotions (
    id         INT PRIMARY KEY,
    name       TEXT NOT NULL,
    starts_on  DATE NOT NULL,               -- первый день акции
    ends_on    DATE NOT NULL                -- последний день акции (включительно)
);

