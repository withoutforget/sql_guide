-- ============================================================================
-- lab003 — Операции над множествами (UNION / INTERSECT / EXCEPT).
-- Схема и тестовые данные. Доменная область: тот же интернет-магазин.
--
-- Данные специально подобраны так, чтобы КАЖДЫЙ оператор давал видимый,
-- непустой результат, и чтобы были видны тонкости дедупликации и NULL:
--
--   * два «сырых» списка контактов из разных каналов привлечения
--     (online_customers и offline_customers). В них ЕСТЬ:
--       - строки, встречающиеся в ОБОИХ списках  → для INTERSECT и дедупа UNION;
--       - строки только в одном из списков        → для EXCEPT;
--       - ДУБЛЬ внутри одного списка (Егор дважды) → UNION ALL vs UNION;
--       - строки с NULL в городе (Глеб)            → показать, что при дедупе
--                                                     NULL считается равным NULL;
--   * заказы устроены так, что есть клиенты, купившие и книгу А, и книгу Б
--     (INTERSECT), купившие А, но не Б (EXCEPT), и товары, которые не заказывал
--     никто (каталог EXCEPT заказанное).
--
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab003 CASCADE;
CREATE SCHEMA lab003;
SET search_path TO lab003;

-- ── Категории товаров ───────────────────────────────────────────────────────
CREATE TABLE categories (
    id    INT  PRIMARY KEY,
    name  TEXT NOT NULL
);

-- ── Товары ──────────────────────────────────────────────────────────────────
CREATE TABLE products (
    id           INT PRIMARY KEY,
    name         TEXT          NOT NULL,
    category_id  INT           NOT NULL REFERENCES categories(id),
    price        NUMERIC(10,2) NOT NULL,   -- цена за единицу, ₽
    in_stock     INT           NOT NULL    -- остаток на складе, шт.
);

-- ── Клиенты (мастер-таблица: канонический справочник с id) ───────────────────
CREATE TABLE customers (
    id          INT  PRIMARY KEY,
    name        TEXT NOT NULL,
    city        TEXT NOT NULL,
    registered  DATE NOT NULL
);

-- ── Заказы (каждая строка — один товар в заказе клиента, как в lab001/lab002) ─
CREATE TABLE orders (
    id           INT  PRIMARY KEY,
    customer_id  INT  NOT NULL REFERENCES customers(id),
    product_id   INT  NOT NULL REFERENCES products(id),
    quantity     INT  NOT NULL,
    ordered_at   DATE NOT NULL
);

-- ── Два «сырых» списка контактов из разных каналов привлечения ───────────────
-- Это НЕ нормализованные таблицы, а именно импортированные списки: без ключей,
-- могут содержать дубликаты и NULL. Такие списки на практике как раз и
-- «складывают»/сравнивают операциями над множествами (слить в один, найти общих
-- клиентов, найти уникальных для канала). Схема у списков одинаковая — три
-- текстовых поля в одном и том же порядке, поэтому их можно объединять.
CREATE TABLE online_customers (      -- пришли через сайт
    email  TEXT,
    name   TEXT,
    city   TEXT
);

CREATE TABLE offline_customers (     -- оформлены в офлайн-магазине
    email  TEXT,
    name   TEXT,
    city   TEXT
);

