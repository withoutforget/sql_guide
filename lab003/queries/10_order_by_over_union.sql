-- ORDER BY применяется КО ВСЕМУ результату объединения и пишется ОДИН раз, в
-- самом конце (нельзя приписать ORDER BY к отдельной ветке без скобок).
-- Сортировать можно по именам колонок ПЕРВОГО запроса (здесь email, city) —
-- имена всего результата берутся именно из него. NULLS LAST отправляет строку с
-- неизвестным городом (Глеб) в конец. Дедупликация UNION идёт по паре
-- (email, city): второй Егор и совпадающие Анна/Глеб схлопываются.
SELECT email, city FROM online_customers
UNION
SELECT email, city FROM offline_customers
ORDER BY city NULLS LAST, email;
