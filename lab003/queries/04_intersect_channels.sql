-- INTERSECT возвращает строки, которые есть в РЕЗУЛЬТАТЕ обоих запросов
-- одновременно (пересечение множеств), и убирает дубликаты. Совпадение — по всей
-- строке. Ответ: Анна и Глеб.
-- Обратите внимание на Глеба: его строки совпали, ХОТЯ город у него NULL. При
-- сопоставлении строк в INTERSECT NULL считается равным NULL — а вот обычное
-- условие c1.city = c2.city (как в JOIN) на двух NULL дало бы UNKNOWN и не
-- сматчило бы их. Это важное отличие семантики.
SELECT email, name, city FROM online_customers
INTERSECT
SELECT email, name, city FROM offline_customers
ORDER BY email;
