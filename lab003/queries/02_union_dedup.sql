-- UNION (без ALL) объединяет строки и УБИРАЕТ дубликаты. Дубликатом считается
-- полное совпадение строки по ВСЕМ колонкам (email, name, city). Поэтому:
--   * Анна и Глеб, попавшие в оба списка, останутся по одному разу;
--   * Егор, внесённый в офлайн-список дважды, тоже схлопнется в одну строку;
--   * у Глеба город NULL в обоих списках — строки совпадают ПОБАЙТОВО (при
--     дедупе NULL считается равным NULL), поэтому Глеб тоже один.
-- Из 10 исходных строк остаётся 7 уникальных клиентов.
SELECT email, name, city FROM online_customers
UNION
SELECT email, name, city FROM offline_customers
ORDER BY email;
