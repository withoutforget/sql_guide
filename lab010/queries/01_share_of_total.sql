-- Задача 1. Доля каждого товара в общей выручке магазина (OVER () — окно без разделов).
SET search_path TO lab010;

-- sum(revenue) OVER () — сумма выручки по ВСЕМ строкам результата (пустое окно =
-- весь результат как один раздел). Она одинакова в каждой строке, поэтому её
-- можно поставить в знаменатель прямо здесь, не собирая итог отдельным запросом.
-- Так GROUP BY не умеет: он схлопнул бы товары и потерял детальную строку.
-- 100.0 * ... — чтобы не сработало ЦЕЛОЧИСЛЕННОЕ деление (иначе получили бы 0).
SELECT
    p.name                                              AS товар,
    p.revenue                                           AS выручка,
    sum(p.revenue) OVER ()                              AS итог_магазина,
    round(100.0 * p.revenue / sum(p.revenue) OVER (), 2) AS доля_проц
FROM products AS p
ORDER BY p.revenue DESC, p.id;
