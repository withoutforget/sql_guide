-- Задача 4. Место товара по выручке внутри его категории (практический RANK-лидерборд).
SET search_path TO lab010;

-- Практическое применение того, что задача 3 сравнивала: даём каждому товару его
-- МЕСТО по выручке в своей категории. Берём rank() — «спортивный» ранг: равные по
-- выручке делят место, а следующий получает ранг с пропуском. Так, в Доме два
-- товара делят 1-е место (оба по 24000), поэтому места идут 1,1,3,4 (ранга 2 нет).
-- Здесь фильтровать не нужно — ранг просто выводится рядом со строкой (окно живёт
-- прямо в SELECT, обёртка не требуется).
SELECT
    c.name                                                  AS категория,
    rank() OVER (PARTITION BY c.name ORDER BY p.revenue DESC) AS место,
    p.name                                                  AS товар,
    p.revenue                                               AS выручка
FROM products  AS p
JOIN categories AS c ON c.id = p.category_id
ORDER BY c.name, место, p.id;
