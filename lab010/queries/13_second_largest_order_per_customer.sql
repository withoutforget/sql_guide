-- Задача 13. 🔥 Второй по величине заказ КАЖДОГО клиента — с честной обработкой ничьих.
SET search_path TO lab010;

-- «Второй по величине» звучит просто, но выбор ранжирующей функции здесь КРИТИЧЕН —
-- это классический капкан на собеседовании. Разберём на данных лабы.
--
-- Правильный инструмент — DENSE_RANK по сумме заказа (DESC), затем dr = 2. Почему
-- именно он (а не row_number и не rank):
--   * Глеб: заказы 3000, 3000, 1500. Максимум — НИЧЬЯ (два по 3000).
--       row_number → 1,2,3: назвал бы «вторым» ОДИН из заказов по 3000 — но это тот
--                   же максимум, а не второй по величине. НЕВЕРНО.
--       rank       → 1,1,3: ранга 2 ВООБЩЕ НЕТ (пропуск после ничьи) — фильтр rank=2
--                   потерял бы Глеба целиком. НЕВЕРНО.
--       dense_rank → 1,1,2: второе РАЗЛИЧНОЕ по величине значение — 1500. ВЕРНО.
--   * Дарья: заказы 24000, 12000, 12000. Ничья на ВТОРОМ месте.
--       dense_rank → 1,2,2: «второй по величине» — это ОБА заказа по 12000, и оба
--                   честно попадают в ответ (row_number оставил бы произвольно один).
-- Итог: dense_rank = «второй по величине РАЗЛИЧНЫЙ результат», и он корректно
-- показывает все заказы, делящие это значение. Ровно то, что нужно.
WITH ranked AS (
    SELECT
        cu.name          AS customer,
        o.id             AS order_id,
        p.name           AS product,
        o.ordered_at,
        p.price * o.qty  AS amount,
        dense_rank() OVER (PARTITION BY o.customer_id
                           ORDER BY p.price * o.qty DESC) AS dr
    FROM orders    AS o
    JOIN products  AS p  ON p.id  = o.product_id
    JOIN customers AS cu ON cu.id = o.customer_id
)
SELECT
    customer   AS клиент,
    order_id   AS заказ,
    product    AS товар,
    amount     AS сумма
FROM ranked
WHERE dr = 2                       -- второй по величине (различный) заказ
ORDER BY клиент, сумма DESC, заказ;
