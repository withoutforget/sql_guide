-- ============================================================================
-- lab010 — Оконные функции: ОСНОВЫ (OVER, PARTITION BY, ранги).
-- Схема и тестовые данные. Доменная область: тот же интернет-магазин.
-- Лаба САМОДОСТАТОЧНА: своя схема lab010, свой init.sql.
--
-- Данные СПЕЦИАЛЬНО подобраны под тему (все числа ниже проверены прогоном):
--
--  (1) НИЧЬИ по выручке товара — ядро лабы. Оконные ROW_NUMBER/RANK/DENSE_RANK
--      различаются ТОЛЬКО там, где есть равные значения. Заложены две ничьи:
--        * Электроника: Планшет и Наушники — по 30 000 ₽ (2-е место делят);
--        * Дом:         Пылесос и Кофеварка — по 24 000 ₽ (делят 1-е место!).
--      Плюс на уровне ВСЕХ товаров ещё и тройка по 3 000 ₽ (SQL, Плед, Мяч).
--      На таких данных:
--        revenue: 30000, 30000  →  ROW_NUMBER 2,3 | RANK 2,2 | DENSE_RANK 2,2
--        revenue: 24000, 24000  →  делят топ-1 категории Дом (важно для top-N).
--
--  (2) ВЫРУЧКА ТОВАРА = price * units_sold (столбец revenue вычисляется
--      автоматически, GENERATED). units_sold в products в точности равен
--      количеству, проданному в orders (таблицы согласованы по построению):
--        Электроника: Смартфон 60000, Планшет 30000, Наушники 30000, Колонка 12000
--        Книги:       Алгоритмы 8000, Рефакторинг 5000, Чистый код 4500, SQL 3000
--        Дом:         Пылесос 24000, Кофеварка 24000, Лампа 6000, Плед 3000
--        Игрушки:     Конструктор 9000, Мяч 3000, Пазл 1500
--        Категории:   Электроника 132000, Дом 57000, Книги 20500, Игрушки 13500
--        ИТОГО по магазину: 223 000 ₽  (и по товарам, и по клиентам сходится).
--
--  (3) ТРАТЫ КЛИЕНТОВ различны (для NTILE — квартили по 7 клиентам):
--        Егор 55000, Дарья 48000, Анна 42000, Борис 38000, Вера 22000,
--        Жанна 10500, Глеб 7500.
--
--  (4) НИЧЬИ на уровне ОТДЕЛЬНЫХ заказов (для «второй по величине заказ клиента»):
--        * Глеб:  заказы на 3000, 3000, 1500 — РАВНЫЙ максимум (два по 3000).
--                 Тут RANK() даст 1,1,3 (ранга 2 нет!), а DENSE_RANK() 1,1,2 —
--                 «второй по величине» честно находит только DENSE_RANK.
--        * Дарья: заказы на 24000, 12000, 12000 — ничья на ВТОРОМ месте:
--                 «второй по величине» — это ОБА заказа по 12000.
--
--  (5) ДАТЫ заказов раскиданы по месяцам 2024-01…2024-04 (для разрезов по месяцу
--      и для дедупликации «последний заказ клиента»).
--
-- Никаких оконных РАМОК (ROWS/RANGE/GROUPS), LAG/LEAD, FIRST_VALUE/LAST_VALUE
-- здесь НЕ используется — это lab011. Тут только PARTITION BY / ORDER BY,
-- ранжирующие и агрегатные окна без явных рамок.
--
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab010 CASCADE;
CREATE SCHEMA lab010;
SET search_path TO lab010;

-- ── Категории товаров ───────────────────────────────────────────────────────
CREATE TABLE categories (
    id    INT  PRIMARY KEY,
    name  TEXT NOT NULL
);

-- ── Товары ──────────────────────────────────────────────────────────────────
-- revenue — ВЫЧИСЛЯЕМЫЙ столбец: выручка товара = price * units_sold. Так все
-- оконные примеры на уровне товара пишутся одной строкой, без предварительной
-- свёртки. units_sold согласован с таблицей orders (см. шапку файла).
CREATE TABLE products (
    id           INT PRIMARY KEY,
    name         TEXT          NOT NULL,
    category_id  INT           NOT NULL REFERENCES categories(id),
    price        NUMERIC(10,2) NOT NULL,   -- цена за единицу, ₽
    units_sold   INT           NOT NULL,   -- продано штук (= сумме qty в orders)
    revenue      NUMERIC(12,2) GENERATED ALWAYS AS (price * units_sold) STORED
);

-- ── Клиенты ─────────────────────────────────────────────────────────────────
-- В Москве ТРИ клиента (Анна, Вера, Егор), в Казани ДВА (Глеб, Жанна) — чтобы
-- были осмысленны разрезы PARTITION BY city.
CREATE TABLE customers (
    id     INT  PRIMARY KEY,
    name   TEXT NOT NULL,
    city   TEXT NOT NULL
);

-- ── Заказы (каждая строка — один товар в заказе клиента, как в прошлых лабах) ─
-- Сумма заказа-строки = products.price * orders.qty. Именно её ранжируем по
-- клиентам в задаче про «второй по величине заказ».
CREATE TABLE orders (
    id           INT  PRIMARY KEY,
    customer_id  INT  NOT NULL REFERENCES customers(id),
    product_id   INT  NOT NULL REFERENCES products(id),
    qty          INT  NOT NULL,
    ordered_at   DATE NOT NULL
);

