-- VALUES (...), (...) — это не только про INSERT: список строк можно поставить
-- прямо в FROM как маленькую таблицу «на лету». Здесь задаём справочник ценовых
-- сегментов: три строки (граница_включительно, верхняя_граница_исключительно, метка).
-- Алиас seg(lo, hi, label) именует колонки. Дальше — обычное тета-соединение
-- (JOIN по диапазону, из lab002): товар попадает в сегмент, если его цена лежит в
-- [lo, hi). Так справочник-классификатор не нужно создавать в схеме — он живёт
-- только внутри запроса.
SELECT p.name,
       p.price,
       seg.label AS segment
FROM products AS p
JOIN (VALUES
        (    0::numeric,  2000::numeric, 'бюджет'),
        ( 2000::numeric, 10000::numeric, 'средний'),
        (10000::numeric,  1e9::numeric,  'премиум')
     ) AS seg(lo, hi, label)
     ON p.price >= seg.lo AND p.price < seg.hi
ORDER BY p.price;
