-- Задача 5. Развернуть теги товаров: по строке на пару (товар, тег) (unnest).
SET search_path TO lab006;

-- unnest(массив) — SRF, превращающая один массив в набор строк: по строке на
-- каждый элемент. Запись «FROM products AS p, unnest(p.tags) AS tag» — это
-- табличная функция, которая ссылается на колонку p.tags соседней таблицы: для
-- КАЖДОГО товара разворачиваем именно его массив тегов. Postgres подключает такие
-- функции автоматически как LATERAL (подробно — в теории про LATERAL), поэтому
-- отдельное слово LATERAL тут писать не нужно.
-- Товар с несколькими тегами даёт несколько строк; товар без тегов («Настольная
-- лампа», массив '{}') не даёт НИ ОДНОЙ строки и в результат не попадает.
SELECT p.name,
       tag.value AS tag
FROM products AS p,
     unnest(p.tags) AS tag(value)
ORDER BY p.id, tag.value;
