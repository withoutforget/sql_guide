-- Задача 1. Числовой ряд 1..10 и рядом квадрат каждого числа (generate_series в FROM).
SET search_path TO lab006;

-- Самое простое применение функции, возвращающей множество (SRF): generate_series
-- порождает готовый столбец чисел, который стоит в FROM как обычная таблица.
-- generate_series(1, 10) выдаёт по строке на каждое число от 1 до 10 включительно;
-- дальше это обычный SELECT — считаем квадрат прямо в списке выборки.
-- Такой «ряд на лету» не хранится в базе: он существует только на время запроса.
SELECT n,
       n * n AS square
FROM generate_series(1, 10) AS g(n)
ORDER BY n;
