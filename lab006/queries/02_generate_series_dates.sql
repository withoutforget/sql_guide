-- generate_series умеет не только числа: с шагом-интервалом он порождает даты/время.
-- generate_series(date '2024-06-01', date '2024-06-10', interval '1 day') выдаёт
-- по строке на КАЖДЫЙ день окна — включая дни, в которые заказов не было. Это
-- «каркас календаря»: к нему потом (задача 11) приклеивают факты, чтобы в отчёте
-- не было пропущенных дней. Функция возвращает timestamp, поэтому приводим ::date.
-- Заодно покажем день недели (0 = воскресенье) — просто чтобы ряд был содержательным.
SELECT d::date              AS day,
       EXTRACT(DOW FROM d)  AS dow          -- номер дня недели (0=вс, 1=пн, ...)
FROM generate_series(date '2024-06-01', date '2024-06-10', interval '1 day') AS g(d)
ORDER BY d;
