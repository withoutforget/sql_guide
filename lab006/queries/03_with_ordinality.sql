-- Задача 3. Этапы доставки с номером шага (WITH ORDINALITY — нумерация строк SRF).
SET search_path TO lab006;

-- Любую функцию-множество можно снабдить приставкой WITH ORDINALITY — тогда к её
-- строкам добавляется ЕЩЁ ОДНА колонка: порядковый номер строки (1, 2, 3, ...).
-- Здесь разворачиваем массив названий этапов доставки в строки (unnest) и просим
-- пронумеровать их. Алиас t(stage, step) даёт имена сразу двум колонкам: значению
-- элемента и его номеру. Порядок номеров совпадает с порядком элементов массива.
-- Это стандартный способ получить «позицию элемента», не заводя счётчик вручную.
SELECT t.step,
       t.stage
FROM unnest(ARRAY['Оформлен', 'Оплачен', 'Собран', 'Передан курьеру', 'Доставлен'])
     WITH ORDINALITY AS t(stage, step)
ORDER BY t.step;
