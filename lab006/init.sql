-- Схема и тестовые данные. Доменная область: тот же интернет-магазин, что и в
-- lab001–lab005, но лаба САМОДОСТАТОЧНА: своя схема lab006, свой init.sql.
--
-- Данные специально подобраны так, чтобы демонстрировать ВСЕ приёмы темы:
--   * даты заказов РАЗБРОСАНЫ по дням и по месяцам (июнь и июль 2024), причём
--     в окне 2024-06-01..2024-06-10 есть ПУСТЫЕ дни (04, 06, 07, 09), а в
--     августе-сентябре заказов нет вовсе
--       → generate_series по дням/месяцам + gap-filling (нули в пустые периоды);
--   * у части клиентов БОЛЬШЕ трёх заказов (Анна — 5, Егор — 4, Дарья — 4),
--     а суммы заказов разные
--       → top-N заказов на клиента через LATERAL + ORDER BY + LIMIT;
--   * есть клиент ВООБЩЕ без заказов (Жанна)
--       → видимая разница CROSS JOIN LATERAL (Жанна пропадает) и
--         LEFT JOIN LATERAL (Жанна остаётся с NULL);
--   * у товаров есть небольшое поле-массив tags (1–3 тега; у лампы — пусто)
--       → лёгкое разворачивание через unnest (полноценные массивы — это lab018).
--
-- Никаких новых конструкций, кроме темы лабы, не нужно: всё решается
-- generate_series / unnest / VALUES / LATERAL + CTE + подзапросами + JOIN +
-- множествами + основами из lab001–lab005. Оконных функций (OVER, lab010) и
-- WITH RECURSIVE (lab007) здесь НЕТ.
--
-- Выручка строки заказа = products.price * orders.quantity (как в lab005).
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab006 CASCADE;
CREATE SCHEMA lab006;
SET search_path TO lab006;

-- ── Категории товаров ───────────────────────────────────────────────────────
CREATE TABLE categories (
    id    INT  PRIMARY KEY,
    name  TEXT NOT NULL
);

-- ── Товары ──────────────────────────────────────────────────────────────────
-- tags — небольшой массив тегов товара. Массивы целиком разбираются в lab018;
-- здесь он нужен ТОЛЬКО чтобы показать unnest (разворачивание массива в строки).
-- У «Настольной лампы» тегов нет (пустой массив '{}') — на ней видно, что
-- unnest пустого массива не даёт ни одной строки.
CREATE TABLE products (
    id           INT PRIMARY KEY,
    name         TEXT          NOT NULL,
    category_id  INT           NOT NULL REFERENCES categories(id),
    price        NUMERIC(10,2) NOT NULL,   -- цена за единицу, ₽
    in_stock     INT           NOT NULL,   -- остаток на складе, шт.
    tags         TEXT[]        NOT NULL DEFAULT '{}'  -- теги (для unnest)
);

-- ── Клиенты ─────────────────────────────────────────────────────────────────
CREATE TABLE customers (
    id           INT  PRIMARY KEY,
    name         TEXT NOT NULL,
    city         TEXT NOT NULL,
    registered   DATE NOT NULL
);

-- ── Заказы (каждая строка — один товар в заказе клиента, как в прошлых лабах) ─
CREATE TABLE orders (
    id           INT  PRIMARY KEY,
    customer_id  INT  NOT NULL REFERENCES customers(id),
    product_id   INT  NOT NULL REFERENCES products(id),
    quantity     INT  NOT NULL,
    ordered_at   DATE NOT NULL
);

