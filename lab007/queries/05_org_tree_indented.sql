-- Задача 5. Всё дерево сотрудников от директора вниз: с уровнем и отступами.
SET search_path TO lab007;

-- Полный обход дерева ОТ КОРНЯ вниз + аккуратный вывод «деревом»:
--   ЯКОРЬ     — корень (manager_id IS NULL), уровень 1;
--   РЕКУРСИЯ  — спускаемся к подчинённым, level = level + 1.
-- Две выразительные детали вывода:
--   * ОТСТУПЫ: repeat('    ', level - 1) даёт по 4 пробела на каждый уровень
--     вложенности, поэтому имя визуально «уезжает» вправо вглубь иерархии;
--   * ПОРЯДОК: чтобы потомки шли сразу под своим родителем (а не вперемешку),
--     копим path — массив id от корня к узлу (path || e.id) — и сортируем ПО НЕМУ.
--     Сортировка по массиву пути = обход в глубину (depth-first): сначала вся ветвь
--     Петра целиком, затем ветвь Ольги. (Стандартная альтернатива ручному пути —
--     предложение SEARCH DEPTH FIRST BY ... SET ord из PG14; см. теорию 03/04.)
-- Ответ: Иван(1) → Пётр(2) → Сергей(3) → Алексей/Дмитрий(4) → Мария(3) → Елена(4)
--         → Ольга(2) → Никита/Анна(3).
WITH RECURSIVE tree AS (
    SELECT id, name, position, 1 AS level, ARRAY[id] AS path
    FROM employees
    WHERE manager_id IS NULL                  -- корень дерева
    UNION ALL
    SELECT e.id, e.name, e.position,
           t.level + 1,
           t.path || e.id                     -- продлеваем путь от корня
    FROM tree t
    JOIN employees e ON e.manager_id = t.id
)
SELECT level,
       repeat('    ', level - 1) || name AS org_chart,   -- отступ по уровню
       position
FROM tree
ORDER BY path;                                -- обход в глубину
