-- Задача 11. 🔥 Все маршруты из Москвы в Екатеринбург с защитой от циклов и выводом пути.
SET search_path TO lab007;

-- Хардкор №2. Теперь нужны не просто достижимые города, а ВСЕ маршруты (пути) из
-- Москвы в Екатеринбург, с показом самого пути и суммарного расстояния. Здесь
-- UNION-дедуп из задачи 10 НЕ подходит: разные маршруты в один город — это разные
-- строки, их терять нельзя. Значит UNION ALL — но тогда цикл Казань→Самара→Уфа→Казань
-- зациклит рекурсию навсегда.
--
-- Защита — РУЧНАЯ, через накопленный ПУТЬ-МАССИВ: в path храним список городов
-- маршрута и в рекурсии добавляем следующий город только если его там ЕЩЁ НЕТ:
--     WHERE NOT r.to_city = ANY(p.path)
-- Так ни один город не посещается дважды в пределах одного маршрута → простые пути,
-- рекурсия конечна. (= ANY(массив) — проверка вхождения в массив; массивы — lab006.)
-- Заодно суммируем distance по рёбрам пути. Финал фильтрует маршруты, дошедшие до
-- Екатеринбурга, и печатает путь строкой (array_to_string).
--   Простых маршрутов Москва→Екатеринбург ровно два (оба идут ...→Самара→Уфа→Ект,
--   различаясь только заходом через Нижний Новгород).
-- Ответ: «Москва → Нижний Новгород → Казань → Самара → Уфа → Екатеринбург» (2530 км)
--        и «Москва → Казань → Самара → Уфа → Екатеринбург» (2550 км).
WITH RECURSIVE paths AS (
    -- якорь: рёбра из Москвы; путь = [Москва, город назначения]
    SELECT from_city,
           to_city,
           ARRAY[from_city, to_city] AS path,
           distance AS total_dist
    FROM routes
    WHERE from_city = 'Москва'
    UNION ALL
    -- рекурсия: продлеваем маршрут ещё на одно ребро, НЕ заходя в уже посещённый город
    SELECT p.from_city,
           r.to_city,
           p.path || r.to_city,
           p.total_dist + r.distance
    FROM paths p
    JOIN routes r ON r.from_city = p.to_city
    WHERE NOT r.to_city = ANY(p.path)        -- защита от цикла: без повторов узлов
)
SELECT array_to_string(path, ' → ') AS route,
       total_dist AS distance_km
FROM paths
WHERE to_city = 'Екатеринбург'
ORDER BY total_dist, route;
