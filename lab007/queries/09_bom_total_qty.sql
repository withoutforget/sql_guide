-- Задача 9. 🔥 Полная потребность в деталях на один Велосипед (суммарное количество).
SET search_path TO lab007;

-- Хардкор №1. «Сколько каждой детали нужно, чтобы собрать один велосипед?» —
-- классический вопрос производственного планирования. Тонкость в том, что одна
-- деталь входит в изделие ПО НЕСКОЛЬКИМ путям и на РАЗНЫХ уровнях, и количества
-- надо (1) правильно УМНОЖИТЬ вниз по каждому пути, а затем (2) СЛОЖИТЬ по всем
-- путям для каждой детали. Ошибиться легко: если не умножать по уровням —
-- недосчитаешь спицы; если не суммировать вхождения — недосчитаешь болты.
--
-- Решение = рекурсивное разузлование (как в задаче 8, с умножением e.qty_total*b.qty)
-- + обычная агрегатная свёртка снаружи: GROUP BY деталь, SUM(qty_total). Никаких
-- оконных функций — только рекурсивный CTE + агрегаты из lab001.
--
-- Проверка руками (важно для BOM — легко ошибиться):
--   Спица  = 2 колеса × 36               = 72
--   Болт   = 4 (Рама) + 2 (Руль) + 2·1 (2 Колеса × 1) = 8   ← три пути, разные уровни
--   Обод/Покрышка/Камера = 2 колеса × 1  = 2 (каждого)
--   Рама 1, Руль 1, Колесо 2.
-- Ответ: Спица 72, Болт 8, Камера 2, Колесо 2, Обод 2, Покрышка 2, Рама 1, Руль 1.
WITH RECURSIVE explosion AS (
    SELECT b.component_id,
           b.qty AS qty_total
    FROM bom b
    WHERE b.assembly_id = 1                     -- разузловываем Велосипед
    UNION ALL
    SELECT b.component_id,
           e.qty_total * b.qty                  -- количество умножается по пути вниз
    FROM explosion e
    JOIN bom b ON b.assembly_id = e.component_id
)
SELECT p.name AS component,
       SUM(e.qty_total) AS total_qty            -- складываем все вхождения детали
FROM explosion e
JOIN parts p ON p.id = e.component_id
GROUP BY p.id, p.name
ORDER BY total_qty DESC, p.name;
