-- Самый маленький рекурсивный CTE — ради понимания того, КАК он исполняется.
-- Три обязательные части:
--   * ЯКОРЬ (anchor)      — «SELECT 1 AS n»: стартовая строка(и), с них всё начинается;
--   * UNION ALL           — склейка якоря с результатом рекурсивной части;
--   * РЕКУРСИВНАЯ часть    — «SELECT n + 1 FROM nums ...»: ссылается на САМ CTE (nums)
--                           и строит новые строки из строк прошлого шага.
-- Условие остановки WHERE n < 5 — обязательно: как только шаг не даёт НОВЫХ строк,
-- рекурсия завершается. Пошагово: {1} → +1 → {2} → {3} → {4} → {5} → шаг из 5 даёт
-- 6, но 6 не проходит WHERE n<5 в СЛЕДУЮЩЕЙ итерации → 0 новых строк → стоп.
-- ВАЖНО: слово RECURSIVE пишется ОДИН раз после WITH, даже если рекурсивный CTE один.
-- Прим.: для простых числовых/датовых рядов на практике берут generate_series
-- (lab006) — он короче и быстрее. Рекурсия здесь только для демонстрации механики.
-- Ответ: 1, 2, 3, 4, 5.
WITH RECURSIVE nums AS (
    SELECT 1 AS n                       -- якорь: одна стартовая строка
    UNION ALL
    SELECT n + 1                        -- рекурсивная часть: из n делаем n+1
    FROM nums
    WHERE n < 5                         -- условие остановки
)
SELECT n FROM nums;
