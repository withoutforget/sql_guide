-- Задача 4. Путь от Алексея (id 6) вверх по начальству до генерального директора.
SET search_path TO lab007;

-- Обратный ход — ПОДЪЁМ по дереву. Идём не «к подчинённым», а «к начальнику»:
--   ЯКОРЬ     — сам Алексей (id = 6), шаг 1;
--   РЕКУРСИЯ  — к текущему сотруднику c находим его начальника (e.id = c.manager_id)
--               и делаем шаг вверх. Останавливается само: у генерального директора
--               manager_id = NULL, join не находит начальника → 0 новых строк → стоп.
-- Попутно строкой path накапливаем цепочку имён (конкатенация ' → '); это тот же
-- приём «нести состояние», что в задаче 2, только состояние — текст.
-- Дерево не имеет циклов, поэтому спец-защита от зацикливания здесь не нужна.
-- Ответ: Алексей → Сергей → Пётр → Иван (4 ступени).
WITH RECURSIVE chain AS (
    -- якорь: стартовый сотрудник
    SELECT id, name, position, manager_id, 1 AS step, name::text AS path
    FROM employees
    WHERE id = 6
    UNION ALL
    -- рекурсия: поднимаемся к начальнику текущего сотрудника
    SELECT e.id, e.name, e.position, e.manager_id,
           c.step + 1,
           c.path || ' → ' || e.name
    FROM chain c
    JOIN employees e ON e.id = c.manager_id
)
SELECT step, name, position, path
FROM chain
ORDER BY step;
