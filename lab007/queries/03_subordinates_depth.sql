-- Первая «настоящая» иерархия: СПУСК по дереву сотрудников. Обычным JOIN тут не
-- обойтись — Петру подчиняются и тимлиды (прямо), и разработчики через тимлидов, а
-- в общем случае глубина заранее неизвестна. Рекурсия решает это естественно:
--   ЯКОРЬ     — прямые подчинённые Петра (manager_id = 2), это уровень 1;
--   РЕКУРСИЯ  — к уже найденным подчинённым (subs) присоединяем тех, чей начальник
--               входит в subs (e.manager_id = s.id), это следующий уровень вниз.
-- depth = s.depth + 1 показывает, на сколько ступеней ниже Петра стоит сотрудник.
-- Ответ: уровень 1 — Сергей, Мария (тимлиды); уровень 2 — Алексей, Дмитрий, Елена.
WITH RECURSIVE subs AS (
    -- якорь: кто подчиняется Петру напрямую
    SELECT id, name, position, 1 AS depth
    FROM employees
    WHERE manager_id = 2
    UNION ALL
    -- рекурсия: подчинённые уже найденных подчинённых
    SELECT e.id, e.name, e.position, s.depth + 1
    FROM subs s
    JOIN employees e ON e.manager_id = s.id
)
SELECT depth, name, position
FROM subs
ORDER BY depth, name;
