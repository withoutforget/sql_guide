-- Задача 10. Все города, достижимые из Москвы (обход графа с циклом; защита через UNION).
SET search_path TO lab007;

-- Переходим от деревьев к настоящему ГРАФУ. routes содержит ЦИКЛ
-- (Казань → Самара → Уфа → Казань), поэтому наивный обход через UNION ALL уходил бы
-- в бесконечность: дойдя до Уфы, снова прыгнул бы в Казань, потом опять в Самару...
-- Здесь защита — сам оператор UNION (без ALL): он УБИРАЕТ ДУБЛИКАТЫ между
-- итерациями (разница UNION/UNION ALL — из lab003). Как только город уже попал в
-- результат, повторно он не добавляется, новых строк не появляется → рекурсия
-- завершается, несмотря на цикл. Для «множества достижимых узлов» (важен сам факт
-- достижимости, а не пути) это самый простой и правильный приём.
--   ЯКОРЬ    — города, куда есть прямой рейс из Москвы;
--   РЕКУРСИЯ — куда можно улететь из уже достижимых городов.
-- Ответ: Казань, Нижний Новгород, Самара, Уфа, Екатеринбург, Новосибирск (6 городов;
-- сама Москва не входит — обратных рейсов в неё нет).
WITH RECURSIVE reach AS (
    SELECT to_city AS city
    FROM routes
    WHERE from_city = 'Москва'
    UNION                                    -- UNION (дедуп!) гасит цикл
    SELECT r.to_city
    FROM reach re
    JOIN routes r ON r.from_city = re.city
)
SELECT city
FROM reach
ORDER BY city;
