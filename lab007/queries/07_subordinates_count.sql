-- Задача на ТРАНЗИТИВНОЕ ЗАМЫКАНИЕ: для каждого начальника посчитать всех, кто под
-- ним прямо или косвенно. Строим пары «(начальник boss_id, подчинённый ему emp_id)»
-- для ВСЕХ пар предок-потомок сразу, а потом просто GROUP BY + COUNT.
--   ЯКОРЬ     — каждый сотрудник сам себе (boss_id = emp_id): пара «узел сам с собой»;
--   РЕКУРСИЯ  — если emp_id — начальник для e, то boss_id тоже начальник для e
--               (наследуем связь на один уровень глубже). Так из прямых связей
--               «начальник→подчинённый» получаем ВСЕ связи «предок→потомок».
-- В финале для каждого boss берём COUNT(*) - 1 (минус он сам). HAVING отсекает тех,
-- у кого подчинённых нет. Это классический приём, который обычным JOIN не выразить
-- (число уровней замыкания неизвестно).
-- Ответ: Иван — 9, Пётр — 5, Ольга — 2, Сергей — 2, Мария — 1.
WITH RECURSIVE closure AS (
    -- якорь: каждый сотрудник в паре сам с собой
    SELECT id AS boss_id, id AS emp_id
    FROM employees
    UNION ALL
    -- рекурсия: начальник начальника — тоже начальник
    SELECT cl.boss_id, e.id
    FROM closure cl
    JOIN employees e ON e.manager_id = cl.emp_id
)
SELECT b.name AS boss,
       count(*) - 1 AS total_subordinates      -- минус сам boss (пара сам-с-собой)
FROM closure cl
JOIN employees b ON b.id = cl.boss_id
GROUP BY b.id, b.name
HAVING count(*) - 1 > 0                         -- только у кого есть подчинённые
ORDER BY total_subordinates DESC, boss;
