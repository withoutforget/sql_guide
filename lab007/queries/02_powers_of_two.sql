-- Задача 2. Степени двойки до 512 с номером шага (нести состояние в рекурсии).
SET search_path TO lab007;

-- Второй разминочный пример: рекурсия несёт СРАЗУ ДВЕ колонки — счётчик шага k и
-- накопленное значение value, которое на каждом шаге УМНОЖАЕТСЯ на 2. Это ключевая
-- идея: рекурсивная часть видит строку предыдущего шага и вычисляет из неё новую
-- (k+1, value*2). Так в рекурсию «протаскивают» уровень, накопленную сумму, путь и
-- т.п. — приём, который дальше используется для глубины дерева и длины маршрута.
-- Остановка: WHERE value < 512 — последний порождённый value = 512 (из 256<512),
-- а из 512 нового шага уже не будет (512 < 512 ложно).
-- Прим.: снова — чистый ряд удобнее генерировать через generate_series (lab006);
-- рекурсия оправдана, когда следующий элемент зависит от предыдущего нетривиально.
-- Ответ: 2^0=1, 2^1=2, ..., 2^9=512 (10 строк).
WITH RECURSIVE powers AS (
    SELECT 0 AS k, 1 AS value            -- якорь: 2^0 = 1
    UNION ALL
    SELECT k + 1, value * 2              -- шаг: следующая степень двойки
    FROM powers
    WHERE value < 512                    -- пока не дошли до 512
)
SELECT k AS exponent, value AS power_of_two
FROM powers
ORDER BY k;
