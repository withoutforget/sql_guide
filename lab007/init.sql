-- ============================================================================
-- lab007 — Рекурсивные CTE (WITH RECURSIVE): иерархии и графы.
-- Схема и тестовые данные. Лаба САМОДОСТАТОЧНА: своя схема lab007, свой init.sql.
--
-- Рекурсивный CTE нужен там, где число «уровней» заранее НЕИЗВЕСТНО: дерево на
-- любую глубину, граф на любое число рёбер. Поэтому под тему заложено СРАЗУ
-- НЕСКОЛЬКО структур — каждая демонстрирует свой класс задач:
--
--   * employees  — дерево сотрудников (директор → отделы → тимлиды → разработчики),
--                  4 уровня. Корень — manager_id IS NULL. Нужно для «спуска» (все
--                  подчинённые) и «подъёма» (путь до директора).
--   * categories — дерево категорий магазина с подкатегориями в 4 уровня
--                  (родитель — parent_id, корни — parent_id IS NULL). Нужно для
--                  дерева с отступами, «хлебных крошек» и уровней.
--   * parts+bom  — состав изделия (bill of materials): велосипед собирается из
--                  узлов, узлы — из деталей. Одна деталь (Болт) входит в изделие
--                  ПО НЕСКОЛЬКИМ путям на РАЗНЫХ уровнях → показывает «разузлование»
--                  с корректным умножением количеств по уровням и суммированием.
--   * routes     — ОРИЕНТИРОВАННЫЙ граф авиамаршрутов между городами. В нём НАРОЧНО
--                  заложен ЦИКЛ (Казань → Самара → Уфа → Казань), чтобы показать,
--                  что без защиты обход графа зацикливается, и как от этого
--                  защищаться (путь-массив, стандартный CYCLE).
--
-- Никаких новых конструкций, кроме WITH RECURSIVE, тут не нужно: всё решается
-- рекурсивным CTE + тем, что пройдено в lab001–lab006 (JOIN, UNION/UNION ALL,
-- подзапросы, обычные CTE, массивы через unnest/|| ). Оконных функций (OVER) тут
-- НЕТ — они в lab010 и здесь ЗАПРЕЩЕНЫ.
--
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab007 CASCADE;
CREATE SCHEMA lab007;
SET search_path TO lab007;

-- ── Сотрудники: дерево «кто кому подчиняется» ───────────────────────────────
-- manager_id ссылается на id ЭТОЙ ЖЕ таблицы (это self-reference, как self-join
-- из lab002). У корня (генеральный директор) manager_id = NULL.
CREATE TABLE employees (
    id          INT  PRIMARY KEY,
    name        TEXT NOT NULL,
    position    TEXT NOT NULL,             -- должность
    manager_id  INT  REFERENCES employees(id)   -- начальник; NULL у самого верха
);

-- ── Категории: дерево «категория → подкатегории» ────────────────────────────
-- parent_id ссылается на id этой же таблицы. У корневых категорий parent_id = NULL.
CREATE TABLE categories (
    id         INT  PRIMARY KEY,
    name       TEXT NOT NULL,
    parent_id  INT  REFERENCES categories(id)   -- родительская категория; NULL у корня
);

-- ── Детали/узлы изделия (bill of materials) ─────────────────────────────────
-- parts — просто справочник «что бывает» (и готовые изделия, и узлы, и мелкие
-- детали лежат в одной таблице — как в реальных PDM/ERP системах).
CREATE TABLE parts (
    id    INT  PRIMARY KEY,
    name  TEXT NOT NULL
);

-- bom (bill of materials) — «из чего состоит»: сборка assembly_id включает
-- component_id в количестве qty штук. Одна строка = «в X входит qty штук Y».
-- Это тоже граф (точнее — направленный ациклический граф, DAG): узел может быть
-- и сборкой (слева), и компонентом другой сборки (справа).
CREATE TABLE bom (
    assembly_id   INT NOT NULL REFERENCES parts(id),   -- что собираем
    component_id  INT NOT NULL REFERENCES parts(id),   -- что в него входит
    qty           INT NOT NULL,                        -- сколько штук на 1 сборку
    PRIMARY KEY (assembly_id, component_id)
);

-- ── Граф маршрутов (С ЦИКЛОМ!) ──────────────────────────────────────────────
-- Ориентированное ребро: из from_city можно улететь в to_city, расстояние distance
-- километров. Граф направленный: наличие рейса A→B не означает рейса B→A.
CREATE TABLE routes (
    from_city  TEXT NOT NULL,
    to_city    TEXT NOT NULL,
    distance   INT  NOT NULL,             -- километров
    PRIMARY KEY (from_city, to_city)
);

