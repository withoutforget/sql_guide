DROP SCHEMA IF EXISTS lab001 CASCADE;
CREATE SCHEMA lab001;
SET search_path TO lab001;

-- ── Товары ──────────────────────────────────────────────────────────────────
CREATE TABLE products (
    id          INT PRIMARY KEY,
    name        TEXT        NOT NULL,
    category    TEXT        NOT NULL,   -- 'electronics', 'books', 'home', 'toys'
    price       NUMERIC(10,2) NOT NULL, -- цена за единицу, в рублях
    in_stock    INT         NOT NULL    -- остаток на складе, шт.
);

-- ── Клиенты ─────────────────────────────────────────────────────────────────
CREATE TABLE customers (
    id          INT PRIMARY KEY,
    name        TEXT        NOT NULL,
    city        TEXT        NOT NULL,
    registered  DATE        NOT NULL    -- дата регистрации
);

-- ── Заказы (каждая строка — один товар в заказе клиента) ────────────────────
CREATE TABLE orders (
    id           INT PRIMARY KEY,
    customer_id  INT  NOT NULL REFERENCES customers(id),
    product_id   INT  NOT NULL REFERENCES products(id),
    quantity     INT  NOT NULL,
    ordered_at   DATE NOT NULL
);

