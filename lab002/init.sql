-- ============================================================================
-- lab002 — JOIN'ы (соединения таблиц). Схема и тестовые данные.
-- Доменная область: тот же интернет-магазин, что и в lab001, но данные
-- специально подобраны так, чтобы демонстрировать все виды соединений:
--   * есть клиент БЕЗ заказов          → виден в LEFT/FULL/анти-джойне;
--   * есть товары, которые никто не купил → видны в LEFT/анти-джойне;
--   * есть категория БЕЗ товаров        → видна в LEFT JOIN categories→products;
--   * есть таблица employees с иерархией → для self-join (сотрудник→руководитель).
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab002 CASCADE;
CREATE SCHEMA lab002;
SET search_path TO lab002;

-- ── Категории товаров ───────────────────────────────────────────────────────
-- Вынесены в отдельную таблицу (в lab001 категория была просто текстом в
-- products). Так появляется связь products.category_id → categories.id, на
-- которой удобно тренировать джойны.
CREATE TABLE categories (
    id    INT  PRIMARY KEY,
    name  TEXT NOT NULL          -- 'Электроника', 'Книги', ...
);

-- ── Товары ──────────────────────────────────────────────────────────────────
CREATE TABLE products (
    id           INT PRIMARY KEY,
    name         TEXT          NOT NULL,
    category_id  INT           NOT NULL REFERENCES categories(id),
    price        NUMERIC(10,2) NOT NULL,   -- цена за единицу, ₽
    in_stock     INT           NOT NULL    -- остаток на складе, шт.
);

-- ── Клиенты ─────────────────────────────────────────────────────────────────
CREATE TABLE customers (
    id          INT  PRIMARY KEY,
    name        TEXT NOT NULL,
    city        TEXT NOT NULL,
    registered  DATE NOT NULL
);

-- ── Заказы (каждая строка — один товар в заказе клиента, как в lab001) ───────
CREATE TABLE orders (
    id           INT  PRIMARY KEY,
    customer_id  INT  NOT NULL REFERENCES customers(id),
    product_id   INT  NOT NULL REFERENCES products(id),
    quantity     INT  NOT NULL,
    ordered_at   DATE NOT NULL
);

-- ── Сотрудники (иерархия для self-join) ─────────────────────────────────────
-- manager_id ссылается на id этой же таблицы. У самого верхнего руководителя
-- manager_id = NULL (над ним никого нет).
CREATE TABLE employees (
    id          INT  PRIMARY KEY,
    name        TEXT NOT NULL,
    position    TEXT NOT NULL,
    manager_id  INT  REFERENCES employees(id),  -- NULL у директора
    salary      NUMERIC(10,2) NOT NULL           -- оклад, ₽/мес
);

