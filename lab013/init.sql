-- ── Заказы ──────────────────────────────────────────────────────────────────
-- Все денежные суммы здесь — ЦЕЛЫЕ рубли (integer), чтобы показать главную
-- ловушку: наивный «средний чек» sum(revenue_rub)/count(*) считается ЦЕЛЫМ
-- делением и врёт. Скидки местами NULL (не «0», а «неизвестно/не применялась»)
-- — для COALESCE и демонстрации, как NULL ломает наивную арифметику.
--   revenue_rub  integer     — сумма заказа, целые ₽;
--   discount_rub integer     — скидка, целые ₽; NULL = скидки не было;
--   items        integer     — число товаров в заказе;
--   is_paid      boolean     — оплачен ли заказ.
-- Глеб (заказ 9) НЕ оплатил ни одного заказа — на нём проверяется защита
-- деления на ноль (NULLIF) в задаче про «чистый средний чек».
CREATE TABLE orders (
    id           integer  PRIMARY KEY,
    customer     text     NOT NULL,
    revenue_rub  integer  NOT NULL,   -- сумма заказа, целые ₽
    discount_rub integer,             -- скидка, целые ₽; NULL = скидки не было
    items        integer  NOT NULL,   -- число товаров в заказе
    is_paid      boolean  NOT NULL    -- оплачен ли заказ
);

