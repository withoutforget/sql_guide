-- ── Товары ──────────────────────────────────────────────────────────────────
-- Здесь собраны РАЗНЫЕ типы колонок, чтобы показать их поведение:
--   id         smallint       — небольшой целочисленный ключ (int2);
--   sku        varchar(8)     — код из 7 символов при пределе varchar(8);
--   price      numeric(10,2)  — ДЕНЬГИ: точный тип, ровно 2 знака (копейки);
--   weight_kg  double precision — вес: ПРИБЛИЖЁННЫЙ тип с плавающей точкой;
--   units      integer        — остаток на складе (штуки);
--   in_stock   boolean        — есть ли в наличии.
-- Веса 0.50 и 2.50 подобраны специально: на них round() для double (банковское
-- округление к чётному) и round() для numeric (обычное, «половина вверх")
-- дают РАЗНЫЙ результат — см. задачу 04.
CREATE TABLE products (
    id         smallint       PRIMARY KEY,
    sku        varchar(8)     NOT NULL,   -- код товара: 7 символов при пределе varchar(8)
    name       text           NOT NULL,
    category   varchar(16)    NOT NULL,   -- 'electronics', 'books', 'home', 'toys'
    price      numeric(10,2)  NOT NULL,   -- цена, ₽ (точные копейки)
    weight_kg  double precision NOT NULL, -- вес, кг (приближённо)
    units      integer        NOT NULL,   -- остаток на складе, шт.
    in_stock   boolean        NOT NULL    -- есть ли в наличии
);

