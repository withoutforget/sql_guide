-- ── «Сырой» импорт ──────────────────────────────────────────────────────────
-- Данные, «пришедшие из внешнего файла»: числа лежат ТЕКСТОМ и требуют
-- приведения. Часть значений «грязные»: с пробелами по краям (приводятся),
-- дробные (numeric — ок, int — ошибка), и совсем битые ('бесплатно', '', 'нет').
-- Битые строки заранее помечены is_valid = false — это честный способ отделить
-- их БЕЗ регулярных выражений (regexp — тема lab015). Приводим только валидные:
-- фильтр WHERE is_valid выполняется ДО вычисления SELECT-выражений, поэтому
-- ::numeric на битых строках не выполняется и запрос не падает.
CREATE TABLE raw_import (
    id          integer  PRIMARY KEY,
    source_name text     NOT NULL,   -- название позиции
    price_text  text     NOT NULL,   -- цена как ТЕКСТ (может быть «грязной»)
    qty_text    text     NOT NULL,   -- количество как ТЕКСТ
    is_valid    boolean  NOT NULL    -- прошла ли строка проверку импорта
);

