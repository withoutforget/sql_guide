-- lab013 — Типы данных и приведения типов. Схема и тестовые данные.
-- Доменная область: интернет-магазин + импорт «сырых» данных.
-- Данные подобраны так, чтобы ДЕМОНСТРИРОВАТЬ ловушки типов:
--   • деньги в numeric(10,2), количества в integer, вес в double precision;
--   • колонка varchar(8) у предела длины (SKU);
--   • boolean-флаги (in_stock, is_paid);
--   • целочисленные суммы, где наивный «средний чек» врёт из-за int-деления;
--   • NULL в скидках/визитах — для COALESCE / NULLIF / GREATEST-LEAST;
--   • канал с visits = 0 — деление на ноль;
--   • «сырая» таблица с числами-как-текст (пробелы, дроби, мусор) для приведения.
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

