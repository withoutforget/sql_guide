-- ── Маркетинговые каналы ────────────────────────────────────────────────────
-- Для «безопасного деления»: конверсия = orders_cnt / visits. У канала
-- «Баннеры» visits = 0 — прямое деление даст ошибку/сюрприз, спасает NULLIF.
-- Плюс тут же ловушка целочисленного деления: orders_cnt / visits (оба int)
-- всегда 0, потому что заказов меньше, чем визитов.
CREATE TABLE channels (
    id         smallint       PRIMARY KEY,
    name       text           NOT NULL,
    visits     integer        NOT NULL,   -- визиты; у «Баннеров» = 0
    orders_cnt integer        NOT NULL,   -- заказы с канала
    cost_rub   numeric(10,2)  NOT NULL    -- затраты на канал, ₽
);

