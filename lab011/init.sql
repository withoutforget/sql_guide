-- ГЛАВНАЯ таблица — sales: дневная выручка каждого товара (временной ряд).
-- Грануляция: одна строка = (товар, день). Данные СПЕЦИАЛЬНО подобраны так,
-- чтобы тема демонстрировалась (все числа ниже проверены прогоном):
--
--  (1) ВРЕМЕННОЙ РЯД по каждому товару (партиция = товар), 5-7 точек в каждой,
--      с ЯВНЫМИ подъёмами, падениями, «плато» (дельта 0) и РЕЗКИМИ скачками —
--      чтобы LAG/LEAD, дельты, рост %, скользящие средние были содержательны:
--        Смартфон:  10000 12000 12000 8000 14000 15000 9000   (плато 02-03,
--                   провал на 04, скачок +6000 на 05, обвал -6000 на 07)
--        Наушники:  5000 4000 8000 8000 6000 9000 7000
--        Кофеварка: 3000 3000 6000 4500 4500 12000 6000       (скачок +7500 на 06)
--        Пылесос:   20000 16000 [—] 24000 [—] 18000 22000     (см. п.3 — ПРОПУСКИ)
--
--  (2) ДУБЛИ КЛЮЧА СОРТИРОВКИ на уровне ВСЕГО магазина. Если считать нарастающий
--      итог по всем продажам «ORDER BY sale_date», то в каждой дате 3-4 строки
--      (несколько товаров продано в один день) — это РОВЕСНИКИ (peers). На них
--      различаются рамка по умолчанию (RANGE — «склеивает» ровесников, даёт всем
--      итог на КОНЕЦ дня) и ROWS (честный построчный итог). См. задачу 02.
--      Итоги по дням (сумма выручки всех товаров за день):
--        03-01: 38000 | 03-02: 35000 | 03-03: 26000 | 03-04: 44500
--        03-05: 24500 | 03-06: 54000 | 03-07: 44000 | ВСЕГО по магазину: 266000
--
--  (3) ПРОПУСКИ в ряду товара «Робот-пылесос»: он продавался НЕ каждый день
--      (нет 03-03 и 03-05). На нём видно различие ROWS (N физических строк
--      назад) и RANGE с ИНТЕРВАЛОМ (N календарных дней назад): при пропуске дня
--      «3 строки назад» и «3 календарных дня назад» — это РАЗНЫЕ множества строк.
--      См. задачу 04.
--
--  (4) ИТОГИ по товару (для «половины выручки» и «доли от лучшего дня»):
--        Смартфон 80000 (половина 40000), Наушники 47000 (23500),
--        Кофеварка 39000 (19500), Пылесос 100000 (50000).
--      Лучший день (max выручки): Смартфон 15000 (06), Наушники 9000 (06),
--        Кофеварка 12000 (06), Пылесос 24000 (04).
--
-- Никаких запрещённых по роадмапу конструкций: только SELECT/JOIN/CTE/подзапросы
-- и ОКОННЫЕ функции. Дат касаемся лишь базово (сравнение, вычитание — даёт число
-- дней); extract/date_trunc/to_char НЕ используем (это lab014).
--
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab011 CASCADE;
CREATE SCHEMA lab011;
SET search_path TO lab011;

-- ── Товары ──────────────────────────────────────────────────────────────────
-- category — обычная текстовая метка (как в lab001). Двух товаров в каждой
-- категории достаточно для разрезов PARTITION BY category, где они нужны.
CREATE TABLE products (
    id        INT  PRIMARY KEY,
    name      TEXT NOT NULL,
    category  TEXT NOT NULL
);

-- ── Дневные продажи (временной ряд) ─────────────────────────────────────────
-- Одна строка = выручка ОДНОГО товара за ОДИН день. Внутри товара sale_date
-- уникальна (кроме пропусков у «Робота-пылесоса»), поэтому по товару порядок
-- строгий — идеально для LAG/LEAD и скользящих средних. А по ВСЕМУ магазину
-- (без PARTITION BY) в одной дате несколько строк — это и есть ровесники для
-- демонстрации ROWS vs RANGE (задача 02).
CREATE TABLE sales (
    id          INT  PRIMARY KEY,
    product_id  INT  NOT NULL REFERENCES products(id),
    sale_date   DATE NOT NULL,
    revenue     NUMERIC(10,2) NOT NULL      -- выручка товара за день, ₽
);

