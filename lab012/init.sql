-- ── Календарь периодов (ярлыки месяцев; период задан числом, см. шапку) ──────
CREATE TABLE periods (
    period  INT  PRIMARY KEY,       -- номер месяца 1..6
    label   TEXT NOT NULL           -- человекочитаемый ярлык
);

-- ── Клиенты ─────────────────────────────────────────────────────────────────
CREATE TABLE customers (
    id    INT  PRIMARY KEY,
    name  TEXT NOT NULL,
    city  TEXT NOT NULL
);

-- ── Заказы (одна строка = одна покупка; period — целый номер месяца) ─────────
CREATE TABLE orders (
    id           INT           PRIMARY KEY,
    customer_id  INT           NOT NULL REFERENCES customers(id),
    period       INT           NOT NULL REFERENCES periods(period),
    category     TEXT          NOT NULL,   -- 'Электроника', 'Книги', 'Дом'
    amount       NUMERIC(12,2) NOT NULL    -- сумма покупки, ₽
);

-- ── Справочник шагов воронки ────────────────────────────────────────────────
CREATE TABLE funnel_steps (
    step       INT  PRIMARY KEY,   -- порядковый номер шага 1..5
    step_name  TEXT NOT NULL
);

-- ── Лог воронки (одна строка = одно событие посетителя на сайте) ─────────────
CREATE TABLE funnel_events (
    id          INT PRIMARY KEY,
    visitor_id  INT NOT NULL,       -- посетитель сайта (не клиент из customers)
    step        INT NOT NULL REFERENCES funnel_steps(step),
    seq         INT NOT NULL        -- порядок события у посетителя (суррогат времени)
);

