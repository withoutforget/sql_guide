-- воронки конверсии, RFM-сегментация, доли и ABC/Парето.
-- Доменная область: интернет-магазин + лог воронки на сайте.
--
-- Это лаба-«капстоун» модуля аналитики: НЕ вводит нового синтаксиса, а СОБИРАЕТ
-- уже пройденное (агрегаты, CASE, FILTER, оконные функции, NTILE, LAG, CTE,
-- JOIN, ROLLUP, generate_series/VALUES) в реальные аналитические отчёты.
-- Лаба САМОДОСТАТОЧНА: своя схема lab012, свой init.sql.
--
-- ── ГЛАВНЫЙ ПРИЁМ С ДАТАМИ ──────────────────────────────────────────────────
-- Функции работы с датами (extract/date_trunc/to_char/age) ещё НЕ пройдены —
-- это тема lab014. Поэтому «период» (месяц) задан ЦЕЛЫМ ЧИСЛОМ в колонке
-- orders.period (1..6 = янв..июн 2024), а человекочитаемые ярлыки лежат в
-- таблице-календаре periods. В реальной жизни период извлекают из даты
-- функциями из lab014; здесь он задан колонкой, чтобы вся когортная арифметика
-- («возраст периода = период_активности − период_когорты») делалась обычным
-- вычитанием целых, без датных функций.
--
-- ── КАК ПОДОБРАНЫ ДАННЫЕ (всё проверено прогоном) ───────────────────────────
--  orders — покупки клиентов. Одна строка = одна покупка (клиент, период,
--    категория, сумма ₽). 12 клиентов, 34 заказа, ИТОГО ровно 1 000 000 ₽.
--
--  (1) PIVOT (категория × период). В сводной есть ОТСУТСТВУЮЩИЕ комбинации →
--      в них выручка 0: Дом не продавался в п.2 и п.6; Книги — в п.4 и п.6.
--      Итоги по категориям: Электроника 690 000, Дом 191 000, Книги 119 000.
--
--  (2) КОГОРТЫ (по периоду ПЕРВОЙ покупки = min(period) клиента):
--        когорта 1 (5 клиентов): Анна, Борис, Вера, Глеб, Дарья
--        когорта 2 (3 клиента):  Егор, Жанна, Зоя
--        когорта 3 (4 клиента):  Игорь, Клара, Лев, Мила
--
--  (3) RETENTION. У клиентов РАЗНЫЕ рисунки активности — специально с «дырками»,
--      чтобы classic и rolling retention РАЗЛИЧАЛИСЬ (активные периоды клиента):
--        Анна  {1,2,3,4,5,6}  верная, активна всегда
--        Борис {1,3}          ДЫРКА: пропустила п.2, вернулась в п.3
--        Вера  {1}            отвалилась сразу (только когортный период)
--        Глеб  {1,2}          продержалась 1 период после старта
--        Дарья {1,4}          вернулась поздно (пропустила п.2, п.3)
--        Егор  {2,3,4,5,6}    верный
--        Жанна {2,4}          ДЫРКА: пропустила п.3, вернулась в п.4
--        Зоя   {2}            отвалилась сразу
--        Игорь {3,4,5,6}      верный
--        Клара {3,4}          продержалась 1 период
--        Лев   {3}            отвалилась сразу
--        Мила  {3,5}          ДЫРКА: пропустила п.4, вернулась в п.5
--      Из-за «дырок» classic retention (активен ИМЕННО в возрасте k) и rolling
--      (активен в возрасте k ИЛИ позже) дают разные матрицы — см. задачи 6-7.
--
--  (4) RFM / ABC. Суммы специально ПЕРЕКОШЕНЫ: 3 «кита» (Анна 300k, Егор 250k,
--      Игорь 200k) дают 75% выручки — принцип Парето. Накопленная доля по
--      клиентам (по убыванию): 30 → 55 → 75 → 81 → 86 → 90 → 93 → 95.5 → 97.5
--      → 98.7 → 99.5 → 100 %. Классы ABC (A≤80, B≤95, C — остальное): A=3, B=4,
--      C=5 клиентов. Границы 80% и 95% каждый раз ПЕРЕСЕКАЕТ один клиент
--      (Глеб — 80%, Дарья — 95%) — удобно для разбора «в какой класс попадает
--      клиент на границе». Все суммы РАЗНЫЕ (нет ничьих в monetary).
--
--  funnel_events — лог воронки на сайте. Отдельная популяция «посетителей»
--    (visitor_id 1..10, НЕ те же, что клиенты). Шаги: 1 Визит → 2 Просмотр →
--    3 Корзина → 4 Оформление → 5 Оплата. seq — порядок событий у посетителя
--    (суррогат времени вместо даты).
--
--  (5) ВОРОНКА монотонно убывает по числу дошедших: 10 → 8 → 5 → 3 → 2.
--      Ровно одна АНОМАЛИЯ ПОРЯДКА: посетитель 4 положил в корзину РАНЬШЕ, чем
--      посмотрел товар (seq корзины < seq просмотра). Поэтому «строгая» воронка
--      (шаги строго по возрастанию seq) на шаге «Корзина» даёт 4, а не 5 —
--      см. задачи 8-9.
-- ============================================================================

DROP SCHEMA IF EXISTS lab012 CASCADE;
