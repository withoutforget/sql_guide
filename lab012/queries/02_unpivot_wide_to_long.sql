-- Иногда данные приходят «широкими»: по столбцу на месяц. Чтобы с ними работать
-- (группировать, джойнить), их разворачивают обратно в «длинный» вид: одна строка
-- на (категория, месяц). Приём: к каждой широкой строке присоединяем LATERAL-набор
-- пар (ярлык, номер, значение) из VALUES и разворачиваем. NULL-ячейки (нет продаж)
-- отбрасываем через WHERE, чтобы в длинной таблице не было пустых строк.
WITH wide(категория, янв, фев, мар) AS (
    VALUES
        ('Электроника', 50000, 155000, 130000),
        ('Книги',       37000,  23000,  17000),
        ('Дом',         30000,   NULL,  48000)   -- в феврале Дом не продавался
)
SELECT w.категория, u.месяц, u.сумма
FROM wide AS w
CROSS JOIN LATERAL (
    VALUES ('янв', 1, w.янв),
           ('фев', 2, w.фев),
           ('мар', 3, w.мар)
) AS u(месяц, ном, сумма)
WHERE u.сумма IS NOT NULL
ORDER BY w.категория, u.ном;
