-- ── Товары ──────────────────────────────────────────────────────────────────
-- Числовые меры для статистики:
--   price       — цена, ₽                (медиана/перцентили/стандартное откл.)
--   units_sold  — сколько штук продано   (для корреляции цена ↔ спрос)
--   rating      — оценка 1..5             (для mode() — самого частого значения)
CREATE TABLE products (
    id          INT PRIMARY KEY,
    name        TEXT          NOT NULL,
    category    TEXT          NOT NULL,   -- 'Электроника', 'Книги', 'Дом', 'Игры'
    price       NUMERIC(10,2) NOT NULL,   -- цена за штуку, ₽
    units_sold  INT           NOT NULL,   -- продано всего, шт.
    rating      INT           NOT NULL    -- средняя оценка покупателей, 1..5
);

-- ── Клиенты ─────────────────────────────────────────────────────────────────
CREATE TABLE customers (
    id     INT PRIMARY KEY,
    name   TEXT NOT NULL,
    city   TEXT NOT NULL
);

-- ── Заказы (шапка) ──────────────────────────────────────────────────────────
CREATE TABLE orders (
    id           INT PRIMARY KEY,
    customer_id  INT  NOT NULL REFERENCES customers(id),
    ordered_at   DATE NOT NULL
);

-- ── Позиции заказа (каждая строка — один товар в заказе) ─────────────────────
-- Именно из этих строк string_agg / array_agg собирают «состав заказа».
CREATE TABLE order_items (
    id          INT PRIMARY KEY,
    order_id    INT NOT NULL REFERENCES orders(id),
    product_id  INT NOT NULL REFERENCES products(id),
    qty         INT NOT NULL
);

