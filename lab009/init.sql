-- lab009 — Статистические и сортирующие/гипотетические агрегаты,
--          агрегаты-коллекции. Схема и тестовые данные.
-- Доменная область: тот же интернет-магазин, что и в прошлых лабах.
--
-- Данные СПЕЦИАЛЬНО подобраны так, чтобы демонстрировать тему (все числа ниже
-- проверены реальным прогоном — см. комментарии):
--
--  (1) ПЕРЕКОС (skew): в категории «Электроника» один очень дорогой товар
--      (Ноутбук, 90 000 ₽) утягивает СРЕДНЕЕ вверх, а МЕДИАНА остаётся
--      «типичной». Цены: 3000, 5000, 7000, 9000, 15000, 90000.
--        avg(price)              = 21 500 ₽   (выше 5 товаров из 6!)
--        median = percentile_cont(0.5) = 8 000 ₽  (типичная цена)
--      Разрыв median≪avg — классический признак перекоса вправо.
--
--  (2) percentile_cont ≠ percentile_disc: при ЧЁТНОМ числе элементов
--      непрерывный перцентиль интерполирует, а дискретный берёт реальное
--      значение из набора.
--        «Электроника» (6 шт): cont(0.5)=8000, disc(0.5)=7000 → РАЗНЫЕ.
--        «Книги»      (4 шт): cont(0.5)=800,  disc(0.5)=700  → РАЗНЫЕ.
--      А при НЕЧЁТНОМ они совпадают (медиана = реальный средний элемент):
--        «Дом»  (5 шт): cont(0.5)=disc(0.5)=3000.
--        «Игры» (5 шт): cont(0.5)=disc(0.5)=2000.
--
--  (3) mode() (самое частое значение) имеет ЯВНОГО победителя: среди рейтингов
--      всех 20 товаров оценка 5 встречается 10 раз, 4 — восемь раз, 3 — два.
--      mode() WITHIN GROUP (ORDER BY rating) = 5.
--
--  (4) КОРРЕЛЯЦИЯ: чем дороже товар, тем меньше его покупают — отрицательная
--      связь цены и продаж (по всем товарам corr(units_sold, price) ≈ -0.46).
--      По категориям: «Дом» -0.93 (почти идеальная прямая), «Электроника»
--      -0.75, «Игры» -0.33, «Книги» +0.05 (связи практически нет). Сильнее
--      всего связь выражена в «Доме» — это ответ хардкорной задачи №14.
--
--  (5) СПИСКИ: заказы (orders + order_items) собираются в читаемые списки
--      товаров через string_agg / array_agg (напр. «состав заказа №1»).
--
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab009 CASCADE;
