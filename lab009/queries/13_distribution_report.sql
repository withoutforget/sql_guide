-- Задача 13. 🔥 Отчёт-«распределение цен» по категориям: центр, разброс, перекос.
SET search_path TO lab009;

-- Полноценная карточка распределения на каждую категорию, собранная РОВНО из
-- пройденных агрегатов (никаких оконных функций). В одной строке — всё, что нужно,
-- чтобы понять форму распределения цен:
--   n               — размер группы;
--   среднее, медиана — два «центра» (расходятся при перекосе);
--   Q1, Q3, IQR     — квартили и межквартильный размах (разброс центральной половины);
--   стд_откл        — стандартное отклонение (разброс вокруг среднего);
--   разрыв          — (среднее − медиана), числовой признак перекоса;
--   форма           — словесный вердикт по разрыву относительно IQR.
-- Квартили берём ОДНИМ вызовом percentile_cont(ARRAY[...]) и раскладываем по индексам.
-- Сортируем по разрыву: самые перекошенные категории — сверху («Электроника»).
WITH stat AS (
    SELECT
        category,
        count(*)                                              AS n,
        round(avg(price), 2)                                  AS среднее,
        (percentile_cont(ARRAY[0.25, 0.5, 0.75])
             WITHIN GROUP (ORDER BY price))::numeric[]         AS q,   -- q[1..3]
        round(stddev_samp(price), 2)                          AS стд_откл
    FROM products
    GROUP BY category
)
SELECT
    category                        AS категория,
    n,
    среднее,
    q[2]                            AS медиана,
    q[1]                            AS q1,
    q[3]                            AS q3,
    q[3] - q[1]                     AS iqr,
    стд_откл,
    round(среднее - q[2], 2)        AS разрыв,
    CASE
        WHEN среднее - q[2] >  0.5 * (q[3] - q[1]) THEN 'перекос вправо (avg ≫ median)'
        WHEN среднее - q[2] < -0.5 * (q[3] - q[1]) THEN 'перекос влево (avg ≪ median)'
        ELSE 'симметрично'
    END                             AS форма
FROM stat
ORDER BY разрыв DESC;
