-- Задача 9. Корреляция цены и спроса: чем дороже товар, тем меньше покупают?
SET search_path TO lab009;

-- corr(Y, X) — число от −1 до +1: насколько две меры движутся вместе по прямой.
--   знак «−» → обратная связь (X растёт → Y падает), «+» → прямая; |corr| — сила.
-- Здесь Y = units_sold (спрос), X = price (цена). Отрицательная связь = «дороже →
-- меньше продаж». По всем товарам ≈ −0.46; по категориям сила разная:
--   «Дом» −0.93 (почти прямая), «Электроника» −0.75, «Игры» −0.33, «Книги» +0.05.
-- (corr симметрична: corr(a,b) = corr(b,a).)

-- 9a) По всему магазину:
SELECT round(corr(units_sold, price)::numeric, 4) AS corr_всего
FROM products;

-- 9b) По категориям, от самой сильной обратной связи:
SELECT
    category                                     AS категория,
    count(*)                                     AS n,
    round(corr(units_sold, price)::numeric, 4)   AS corr_спрос_цена
FROM products
GROUP BY category
ORDER BY corr_спрос_цена;
