-- Задача 9. «Возможно, вы имели в виду...»: отсортировать бренды по близости к тому, что ввёл пользователь.
SET search_path TO lab016;

-- Оператор расстояния a <-> b = 1 - similarity(a,b): 0 — идентичны, 1 — ничего общего.
-- Сортировка по <-> ставит самых похожих наверх — это и есть автодополнение /
-- «возможно, вы имели в виду». Пользователь набрал «ксиоме» (такого написания в
-- справочнике НЕТ) — предлагаем ближайшие реальные варианты. Тай-брейкер по id
-- обязателен: «Ксиоми» и «ксиоми» дают одинаковое расстояние.
SELECT
    id,
    raw_name,
    round((raw_name <-> 'ксиоме')::numeric, 3) AS distance
FROM brands
ORDER BY raw_name <-> 'ксиоме', id
LIMIT 5;
