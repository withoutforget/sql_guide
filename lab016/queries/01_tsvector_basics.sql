-- Задача 1. Разобрать текст в tsvector: как 'russian' лемматизирует и режет стоп-слова, чем отличается 'simple'.
SET search_path TO lab016;

-- to_tsvector(конфиг, текст) → отсортированный набор лемм с позициями.
-- 'russian' приводит слова к основе (ноутбуки→ноутбук, игровой→игров, учёбы→учеб)
-- и выбрасывает стоп-слова (для, и, на); 'simple' только приводит к нижнему
-- регистру и разбивает на токены, ничего не отбрасывая и не нормализуя.
SELECT
    to_tsvector('russian', 'Быстрые игровые ноутбуки для работы и учёбы') AS russian_tsv,
    to_tsvector('simple',  'Быстрые игровые ноутбуки для работы и учёбы') AS simple_tsv;

-- Тот же разбор на реальном описании товара: видно позиции лемм в документе.
SELECT id, name,
       to_tsvector('russian', description) AS description_tsv
FROM products
WHERE id = 1;
