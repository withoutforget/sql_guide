-- Задача 10. Исправление опечатки по расстоянию Левенштейна: найти ближайший термин из справочника (≤ 2 правок).
SET search_path TO lab016;

-- levenshtein(a,b) — минимальное число правок (вставок/удалений/замен символа),
-- чтобы получить из a строку b. В отличие от триграмм это ТОЧНОЕ целое расстояние,
-- удобное для «строгого» исправления опечаток по словарю. Для каждого набранного
-- с ошибкой слова берём из справочника terms ближайший термин с расстоянием ≤ 2.
-- Тай-брейкер по dist, term — на случай одинаковых расстояний до разных терминов.
WITH typed(word) AS (
    VALUES ('ноудбук'), ('планшут'), ('клавеатура'), ('калонка'), ('телевзор')
)
SELECT
    t.word            AS typed,
    d.term            AS suggestion,
    levenshtein(t.word, d.term) AS edits
FROM typed t
JOIN terms d ON levenshtein(t.word, d.term) <= 2
ORDER BY t.word, edits, d.term;
