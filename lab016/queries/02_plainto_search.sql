-- Задача 2. Поиск товаров по описанию с учётом словоформ: запрос «ноутбук» находит «ноутбуки», «ноутбука».
SET search_path TO lab016;

-- plainto_tsquery('russian','ноутбук') → 'ноутбук' (лемма). Оператор @@ проверяет,
-- есть ли эта лемма в векторе описания. Поэтому находятся и «ноутбуки» (товар 1),
-- и «ноутбука» (товары 2, 3) — все они дают ту же основу «ноутбук».
-- LIKE '%ноутбук%' тут случайно тоже сработал бы (основа — префикс), но на других
-- словах (см. задачу 13) он словоформы уже не ловит.
SELECT
    id,
    name,
    description
FROM products
WHERE to_tsvector('russian', description) @@ plainto_tsquery('russian', 'ноутбук')
ORDER BY id;
