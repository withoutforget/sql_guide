-- Задача 8. Триграммное сходство: найти все написания бренда, похожие на «самсунг», несмотря на опечатки.
SET search_path TO lab016;

-- Триграмма — тройка подряд идущих символов. similarity(a,b) ∈ [0,1] — доля общих
-- триграмм. Оператор a % b истинен, когда similarity(a,b) ≥ порога
-- pg_trgm.similarity_threshold (по умолчанию 0.3). Регистр и лишние пробелы на
-- триграммы не влияют, поэтому «Самсунг», «самсунг», «Самсунг » неотличимы (1.0),
-- «Самунг» (опечатка) — 0.5, «Самсунг Электроникс» — 0.4, а «Сяоми»/латиница
-- отсеиваются (общих триграмм почти нет).
SELECT
    id,
    raw_name,
    round(similarity(raw_name, 'самсунг')::numeric, 3) AS sim
FROM brands
WHERE raw_name % 'самсунг'
ORDER BY sim DESC, id;
