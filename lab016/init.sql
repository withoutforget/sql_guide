-- GIN-индекс по вектору: ускоряет `search_tsv @@ query`. Задачи работают и без
-- него — он лишь показывает, как FTS делают быстрой на больших таблицах (lab024).
CREATE INDEX IF NOT EXISTS products_search_gin ON products USING gin (search_tsv);
-- GIN-индекс по триграммам названия: ускоряет LIKE '%...%', similarity() и % (lab024).
CREATE INDEX IF NOT EXISTS products_name_trgm  ON products USING gin (name gin_trgm_ops);

