-- ============================================================================
-- lab016 — Полнотекстовый и нечёткий (fuzzy) поиск. Схема и тестовые данные.
-- Доменная область: каталог интернет-магазина электроники + отзывы + «грязный»
-- справочник брендов и лог пользовательских запросов с опечатками.
-- Скрипт идемпотентен: всё пересоздаётся с нуля при каждом запуске.
--
-- ── ВНИМАНИЕ: расширения и search_path ──────────────────────────────────────
-- Тема требует трёх contrib-расширений: pg_trgm (триграммы), fuzzystrmatch
-- (levenshtein/фонетика), unaccent (снятие диакритики). Раннер (.lib/common.sh)
-- в каждом queries/NN_*.sql делает `SET search_path TO lab016;` — БЕЗ public!
-- Поэтому функции и операторы расширений (similarity, levenshtein, unaccent,
-- операторы % и <->) должны быть НАХОДИМЫ при search_path = lab016. Решение:
-- ставим расширения ПРЯМО В СХЕМУ lab016 (WITH SCHEMA lab016). Тогда их объекты
-- лежат в lab016 и видны запросам без каких-либо оговорок.
--
-- Идемпотентность (главная зона риска этой лабы):
--   1. Сначала БЕЗУСЛОВНО дропаем расширения глобально (DROP EXTENSION ...
--      CASCADE). Это снимает их, даже если от прошлых экспериментов они «утекли»
--      в public: тогда CREATE EXTENSION IF NOT EXISTS ... WITH SCHEMA lab016
--      увидел бы их «уже существующими» и оставил бы в public (вне search_path).
--   2. Потом пересоздаём схему (DROP/CREATE SCHEMA lab016).
--   3. Ставим расширения заново уже В lab016.
-- Порядок «DROP EXTENSION → DROP SCHEMA → CREATE SCHEMA → CREATE EXTENSION»
-- надёжен при любом исходном состоянии базы. Прогон дважды подряд даёт exit 0.
--
-- Данные подобраны так, чтобы ДЕМОНСТРИРОВАТЬ тему:
--   • products.name/description — русский текст со СЛОВОФОРМАМИ (ноутбук/ноутбуки/
--     ноутбука, игр/игры/игровой) и СТОП-СЛОВАМИ (для/и/на/с) → FTS по леммам,
--     веса setweight (заголовок A, описание D), генерируемый tsvector-столбец;
--   • products.description содержит «игры» лишь у одного товара, а форму «игр» —
--     у нескольких → на запросе «игры» видно: LIKE находит 1, FTS — все;
--   • reviews.body — тексты подлиннее с повторами слова «ноутбук» → ранжирование
--     ts_rank/ts_rank_cd и подсветка ts_headline;
--   • brands.raw_name — один бренд, записанный по-разному (регистр, пробелы,
--     опечатки, транслитерация) → similarity/% (похожие), <-> (ближайшие,
--     «возможно, вы имели в виду»), дедуп по нечёткому сходству self-join;
--   • terms.term — канонический справочник категорий → исправление опечаток
--     levenshtein (ближайшее слово, расстояние ≤ 2);
--   • foreign_names.name — латиница с диакритикой → unaccent (поиск без акцентов).
-- ============================================================================

-- Приглушаем NOTICE (сообщения «... does not exist, skipping» и «drop cascades
-- to ...» от идемпотентных DROP-ов) — чтобы вывод прогона был чистым и одинаковым
-- независимо от исходного состояния базы. На сессию init.sql; запросов не касается.
SET client_min_messages = warning;

-- 1) Снять расширения глобально (в т.ч. случайно «утёкшие» в public) — см. шапку.
DROP EXTENSION IF EXISTS pg_trgm       CASCADE;
DROP EXTENSION IF EXISTS fuzzystrmatch CASCADE;
DROP EXTENSION IF EXISTS unaccent      CASCADE;

-- 2) Пересоздать схему с нуля.
DROP SCHEMA IF EXISTS lab016 CASCADE;
CREATE SCHEMA lab016;
SET search_path TO lab016;

-- 3) Поставить расширения ИМЕННО в схему lab016 (объекты будут видны запросам,
--    у которых search_path = lab016).
CREATE EXTENSION IF NOT EXISTS pg_trgm       WITH SCHEMA lab016;
CREATE EXTENSION IF NOT EXISTS fuzzystrmatch WITH SCHEMA lab016;
CREATE EXTENSION IF NOT EXISTS unaccent      WITH SCHEMA lab016;

-- ── Товары: каталог электроники (название + описание на русском) ─────────────
-- search_tsv — ГЕНЕРИРУЕМЫЙ столбец: название с весом 'A' (важнее) и описание с
-- весом 'D' (менее важно), склеены оператором ||. Хранится (STORED) и обновляется
-- автоматически. Так на практике держат вектор рядом с документом; GIN-индекс по
-- нему ускоряет @@ (подробно про индексы — lab024). Задачи умеют считать вектор и
-- на лету (to_tsvector(...)), поэтому от индекса/столбца они НЕ зависят — столбец
-- лишь показывает, «как хранят» вектор.
CREATE TABLE products (
    id          INT  PRIMARY KEY,
    name        TEXT NOT NULL,          -- заголовок (вес A)
    description TEXT NOT NULL,          -- описание (вес D)
    price       NUMERIC(10,2) NOT NULL, -- цена, ₽
    search_tsv  tsvector GENERATED ALWAYS AS (
        setweight(to_tsvector('russian', name),        'A') ||
        setweight(to_tsvector('russian', description),  'D')
    ) STORED
);

