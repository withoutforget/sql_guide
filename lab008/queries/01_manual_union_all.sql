-- Задача 1. «Вручную»: выручка по категориям и общий итог одним результатом (UNION ALL двух GROUP BY).
SET search_path TO lab008;

-- Классический «ручной» способ собрать отчёт с итогом (так делали до расширенной
-- группировки): каждый уровень считаем отдельным GROUP BY и склеиваем ветки через
-- UNION ALL (приём из lab003). Ветка 1 — детализация по категориям; ветка 2 —
-- общий итог по всей таблице (агрегат без GROUP BY = одна строка). Литерал
-- 'ВСЕ КАТЕГОРИИ' подписывает итоговую строку. Минусы подхода: ДВА прохода по
-- sales и много почти одинакового текста. В задаче 2 получим ТО ЖЕ короче —
-- одним GROUP BY GROUPING SETS. ORDER BY revenue кладёт итог (1 000 000) вниз.
SELECT category, SUM(amount) AS revenue      -- уровень 1: по категориям
FROM sales
GROUP BY category
UNION ALL
SELECT 'ВСЕ КАТЕГОРИИ', SUM(amount)          -- уровень 2: общий итог
FROM sales
ORDER BY revenue;
