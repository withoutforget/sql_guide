-- ============================================================================
-- lab008 — Расширенная группировка (GROUPING SETS / ROLLUP / CUBE).
-- Схема и тестовые данные. Доменная область: тот же интернет-магазин,
-- но взгляд «аналитика»: одна денормализованная таблица продаж (витрина/факт),
-- у которой сразу НЕСКОЛЬКО измерений (категория, подкатегория, регион, канал).
-- Именно много измерений и нужно, чтобы строить многомерные итоги.
--
-- Данные специально подобраны так, чтобы:
--   * общий итог по выручке был круглым — ровно 1 000 000 ₽ (легко сверять
--     подытоги вручную: сумма детальных строк = подытог = общий итог);
--   * была естественная иерархия  категория → подкатегория  (для ROLLUP);
--   * два независимых измерения  категория × канал  (для CUBE);
--   * в колонке region встречался НАСТОЯЩИЙ NULL («регион не указан») — это
--     нужно, чтобы показать, зачем нужна функция GROUPING(): в итоговых строках
--     сгруппированная колонка тоже становится NULL, и без GROUPING() «настоящий»
--     NULL региона не отличить от «итогового» NULL строки-итога.
--
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab008 CASCADE;
CREATE SCHEMA lab008;
SET search_path TO lab008;

-- ── Продажи (факт-таблица витрины) ──────────────────────────────────────────
-- Одна строка = агрегированная продажа по сочетанию измерений. Таблица нарочно
-- денормализована (все измерения — прямо здесь, без справочников): тема лабы —
-- ГРУППИРОВКА, а не соединения, поэтому лишние JOIN только мешали бы читать.
--
--   category    — категория товара        (Электроника, Книги, Дом)
--   subcategory — подкатегория             (иерархия: входит в категорию)
--   region      — регион продажи; МОЖЕТ быть NULL («не указан») — это важно!
--   channel     — канал: 'online' / 'offline'
--   amount      — выручка, ₽ (мера)
--   qty         — сколько штук продано (вторая мера)
CREATE TABLE sales (
    id           INT PRIMARY KEY,
    category     TEXT          NOT NULL,   -- измерение 1: категория
    subcategory  TEXT          NOT NULL,   -- измерение 2: подкатегория (внутри категории)
    region       TEXT,                     -- измерение 3: регион (МОЖЕТ быть NULL)
    channel      TEXT          NOT NULL,   -- измерение 4: канал продаж
    amount       NUMERIC(10,2) NOT NULL,   -- мера: выручка, ₽
    qty          INT           NOT NULL    -- мера: количество, шт.
);

-- ── Данные ──────────────────────────────────────────────────────────────────
