--   Книги:       50+30+40+30     = 150 тыс.  (Учебная 80,  Детская 70)
--   Дом:         80+70+60+40     = 250 тыс.  (Кухня 150,   Освещение 100)
--   ─────────────────────────────────────────
--   ОБЩИЙ ИТОГ:                    1 000 тыс. = 1 000 000 ₽
--
