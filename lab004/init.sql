-- Доменная область: тот же интернет-магазин, что и в lab001–lab003.
--
-- Данные специально подобраны так, чтобы демонстрировать ВСЕ приёмы темы:
--   * широкий разброс цен и несколько товаров в каждой категории
--       → скалярные подзапросы (дороже/дешевле общего и КАТЕГОРИЙНОГО среднего);
--   * в категории «Дом» ДВА товара дороже среднего по своей категории, а не один
--       → видно отличие «дороже среднего в категории» от «самый дорогой в категории»;
--   * есть товары, которые НИКТО не заказывал (категория «Спорт» и др.)
--       → NOT IN / NOT EXISTS / анти-соединение;
--   * у клиентов есть колонка referrer_id (кто пригласил), где ЕСТЬ NULL
--       → наглядная ловушка NOT IN + NULL и её обход через NOT EXISTS;
--   * ровно один клиент (Анна) купил ВСЕ книги, и ровно один товар (Наушники)
--     заказан ВСЕМИ москвичами
--       → реляционное деление через двойной NOT EXISTS (задачи 13, 14);
--   * есть клиент вообще без заказов (Захар) и клиенты без единой книги
--       → полу-/анти-соединения через EXISTS / NOT EXISTS.
--
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab004 CASCADE;
CREATE SCHEMA lab004;
SET search_path TO lab004;

-- ── Категории товаров ───────────────────────────────────────────────────────
CREATE TABLE categories (
    id    INT  PRIMARY KEY,
    name  TEXT NOT NULL
);

-- ── Товары ──────────────────────────────────────────────────────────────────
CREATE TABLE products (
    id           INT PRIMARY KEY,
    name         TEXT          NOT NULL,
    category_id  INT           NOT NULL REFERENCES categories(id),
    price        NUMERIC(10,2) NOT NULL,   -- цена за единицу, ₽
    in_stock     INT           NOT NULL    -- остаток на складе, шт.
);

-- ── Клиенты ─────────────────────────────────────────────────────────────────
-- referrer_id — «кто пригласил этого клиента» (реферальная программа): ссылка на
-- id другого клиента ИЗ ЭТОЙ ЖЕ таблицы. NULL означает «пришёл сам, без
-- приглашения». Именно этот NULL нужен, чтобы показать ловушку NOT IN + NULL:
-- наивный запрос «кто никого не приглашал» через NOT IN (SELECT referrer_id ...)
-- сломается, потому что подзапрос вернёт в том числе NULL (см. задачу 5).
CREATE TABLE customers (
    id           INT  PRIMARY KEY,
    name         TEXT NOT NULL,
    city         TEXT NOT NULL,
    registered   DATE NOT NULL,
    referrer_id  INT  REFERENCES customers(id)   -- NULL = пришёл без приглашения
);

-- ── Заказы (каждая строка — один товар в заказе клиента, как в прошлых лабах) ─
CREATE TABLE orders (
    id           INT  PRIMARY KEY,
    customer_id  INT  NOT NULL REFERENCES customers(id),
    product_id   INT  NOT NULL REFERENCES products(id),
    quantity     INT  NOT NULL,
    ordered_at   DATE NOT NULL
);

