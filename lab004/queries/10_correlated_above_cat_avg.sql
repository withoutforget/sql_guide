-- Задача 10. Товары дороже средней цены в СВОЕЙ категории (коррелированный скалярный подзапрос).
SET search_path TO lab004;

-- Порог сравнения СВОЙ у каждой строки: подзапрос считает среднюю цену той
-- категории, к которой относится текущий товар p (корреляция через
-- p2.category_id = p.category_id). Одним фиксированным числом это не выразить.
-- Разные алиасы обязательны: внешняя таблица p, внутренняя p2.
-- В "Доме" среднее ≈ 7990 ₽, и выше него сразу ДВА товара (Кофеварка 8990 и
-- Робот-пылесос 12990) — поэтому результат (6 строк) отличается от "самый дорогой
-- в категории" (задача 12, 5 строк).
SELECT p.name, p.price, p.category_id
FROM products AS p
WHERE p.price > (
    SELECT AVG(p2.price)
    FROM products AS p2
    WHERE p2.category_id = p.category_id
)
ORDER BY p.category_id, p.price DESC;
