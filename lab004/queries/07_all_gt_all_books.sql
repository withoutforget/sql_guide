-- Задача 7. Игрушки дороже ВСЕХ книг (> ALL) и самый дорогой товар каталога (>= ALL).
SET search_path TO lab004;

-- Два запроса — оба про ALL, который требует, чтобы сравнение было верно для ВСЕХ
-- элементов подзапроса.

-- 1) Игрушки дороже ВСЕХ книг. "price > ALL (книги)" истинно, только если цена
--    больше каждой книги, то есть больше самой дорогой (2490) → > ALL == "больше
--    максимума". Проходит один Конструктор (2790). Сравните с задачей 6: тот же
--    список книг, но ANY дал 2 товара, а ALL — 1.
SELECT name, price
FROM products
WHERE category_id = 4
  AND price > ALL (SELECT price FROM products WHERE category_id = 2)
ORDER BY price;

-- 2) Самый дорогой товар всего каталога через >= ALL: цена, которая не меньше
--    всех остальных, — это максимум. Приём находит максимум без агрегата в WHERE.
SELECT name, price
FROM products
WHERE price >= ALL (SELECT price FROM products);
