-- Задача 6. Игрушки, которые дороже хотя бы одной книги (> ANY).
SET search_path TO lab004;

-- ANY ставит оператор (здесь >) между ценой товара и КАЖДОЙ ценой из подзапроса
-- и требует, чтобы сравнение было верно ХОТЯ БЫ для одного элемента.
-- Цены книг = {890, 1290, 2490}. "price > ANY (книги)" истинно, если цена больше
-- самой дешёвой книги (890), то есть > ANY == "больше минимума списка".
-- Проходят Мяч (990) и Конструктор (2790); Пазл (690) — нет.
-- (Тождества: = ANY эквивалентно IN, > ANY эквивалентно "больше минимума".)
SELECT name, price
FROM products
WHERE category_id = 4                                                  -- Игрушки
  AND price > ANY (SELECT price FROM products WHERE category_id = 2)   -- цены книг
ORDER BY price;
