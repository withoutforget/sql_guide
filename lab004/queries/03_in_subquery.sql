-- Задача 3. Клиенты, сделавшие хотя бы один заказ (полу-соединение через IN).
SET search_path TO lab004;

-- Подзапрос возвращает СТОЛБЕЦ id всех клиентов, встречающихся в заказах.
-- IN оставляет клиентов, чей id есть в этом столбце. Дубликаты в подзапросе не
-- мешают (IN проверяет лишь факт наличия), поэтому клиент не размножается и
-- DISTINCT не нужен — в отличие от того же полу-соединения через JOIN (lab002).
-- Захар (id 8) заказов не делал → его в результате нет.
SELECT id, name
FROM customers
WHERE id IN (SELECT customer_id FROM orders)
ORDER BY id;
