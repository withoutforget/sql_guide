-- Задача 12. Самый дорогой товар в каждой категории (строчный подзапрос, сравнение кортежей).
SET search_path TO lab004;

-- Подзапрос строит по одной ПАРЕ (категория, её максимальная цена) на категорию.
-- Внешний запрос оставляет товары, чей кортеж (category_id, price) есть в этом
-- списке пар — то есть чья цена равна максимуму ИМЕННО своей категории.
-- Одной колонкой так нельзя: цену надо сопоставлять ВМЕСТЕ с категорией, иначе
-- перепутаются максимумы разных категорий.
-- Отличие от задачи 10: здесь ровно "самый дорогой" (5 строк), а не "все, кто
-- выше среднего" (6 строк) — в "Доме" Кофеварка выше среднего, но не максимум.
SELECT p.name, p.category_id, p.price
FROM products AS p
WHERE (p.category_id, p.price) IN (
    SELECT category_id, MAX(price)
    FROM products
    GROUP BY category_id
)
ORDER BY p.category_id;
