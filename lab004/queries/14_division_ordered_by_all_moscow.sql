-- Изюм: то же деление, что в задаче 13, но "делим" наоборот. Роли поменялись:
-- кандидат — товар, покрыть надо множество москвичей.
--   "товар заказан КАЖДЫМ москвичом"  ==
--   "НЕ существует москвича, который этот товар НЕ заказывал".
-- Структура двойного NOT EXISTS идентична задаче 13 — освоив шаблон, решаешь
-- весь класс вопросов "покрывает все ...". Москвичи — Анна, Вера, Егор; товар,
-- который заказали все трое, единственный — Наушники.
SELECT p.id, p.name
FROM products AS p
WHERE NOT EXISTS (
    SELECT 1
    FROM customers AS c
    WHERE c.city = 'Москва'                     -- множество, которое надо покрыть: все москвичи
      AND NOT EXISTS (
          SELECT 1
          FROM orders AS o
          WHERE o.customer_id = c.id
            AND o.product_id  = p.id            -- связь "этот москвич заказал товар"
      )
)
ORDER BY p.id;
