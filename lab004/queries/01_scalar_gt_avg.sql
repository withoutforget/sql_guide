-- Задача 1. Товары дороже средней цены по всему каталогу (скалярный подзапрос в WHERE).
SET search_path TO lab004;

-- Средняя цена — это агрегат по ВСЕЙ таблице, а в WHERE агрегаты запрещены
-- (правило из lab001). Поэтому среднее считает ОТДЕЛЬНЫЙ скалярный подзапрос
-- (1 строка × 1 колонка ≈ 9643.33), а внешний WHERE сравнивает с этим числом
-- цену каждого товара. Подзапрос некоррелированный — считается один раз.
SELECT name, price
FROM products
WHERE price > (SELECT AVG(price) FROM products)
ORDER BY price DESC;
