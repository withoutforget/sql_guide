-- Изюм: вопрос со словом "ВСЕ" — это реляционное деление, у которого нет своего
-- оператора в SQL. Его выражают двойным отрицанием:
--   "клиент купил ВСЕ книги"  ==  "НЕ существует книги, которую он НЕ покупал".
-- Два вложенных NOT EXISTS:
--   * внутренний  — "клиент c НЕ покупал книгу p" (нет такого заказа);
--   * внешний     — "нет ни одной книги p, для которой верно внутреннее" =
--                    клиент покрыл все книги.
-- Внутренний подзапрос коррелирует СРАЗУ на два уровня: c.id (клиент) и p.id
-- (книга). У нас все три книги купила только Анна (Дарья взяла две из трёх).
SELECT c.id, c.name
FROM customers AS c
WHERE NOT EXISTS (
    SELECT 1
    FROM products AS p
    WHERE p.category_id = 2                     -- множество, которое надо покрыть: все книги
      AND NOT EXISTS (
          SELECT 1
          FROM orders AS o
          WHERE o.customer_id = c.id
            AND o.product_id  = p.id            -- связь "клиент купил эту книгу"
      )
)
ORDER BY c.id;
