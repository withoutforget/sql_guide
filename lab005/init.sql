-- lab005 — Обобщённые табличные выражения (CTE, оператор WITH).
-- Схема и тестовые данные. Доменная область: тот же интернет-магазин, что и в
-- lab001–lab004, но лаба САМОДОСТАТОЧНА: своя схема lab005, свой init.sql.
--
-- Данные подобраны так, чтобы на них было удобно строить МНОГОШАГОВУЮ аналитику
-- (ровно то, ради чего нужны CTE):
--   * выручка (price * quantity) заметно различается по КАТЕГОРИЯМ — есть явные
--     лидеры (Электроника, Дом) и аутсайдеры (Игрушки, Спорт)
--       → «категории выше средней выручки» даёт непустой, но и не полный ответ;
--   * выручка различается по КЛИЕНТАМ — часть тратит выше среднего чека на
--     клиента, часть ниже (причём Вера — пограничный случай, чуть ниже среднего)
--       → «клиенты выше средней траты»;
--   * в нескольких городах ПО НЕСКОЛЬКУ клиентов (Москва — 3, Казань — 2)
--       → выручка по городам и сравнение клиентов ВНУТРИ города (self-join CTE);
--   * категории по-разному ведут себя в двух измерениях: Дом даёт много выручки,
--     но узкой аудиторией (2 покупателя), Книги — наоборот, много покупателей,
--     но мало денег, и лишь Электроника сильна в ОБОИХ
--       → хардкор-задача «категории выше среднего и по деньгам, И по охвату».
--
-- Никаких новых конструкций тут не нужно: всё решается CTE + подзапросами +
-- JOIN + множествами + основами из lab001–lab004. Оконных функций (OVER) и
-- WITH RECURSIVE здесь НЕТ — они в следующих лабах.
--
-- Скрипт идемпотентен: всё пересоздаётся с нуля.
-- ============================================================================

DROP SCHEMA IF EXISTS lab005 CASCADE;
CREATE SCHEMA lab005;
SET search_path TO lab005;

-- ── Категории товаров ───────────────────────────────────────────────────────
CREATE TABLE categories (
    id    INT  PRIMARY KEY,
    name  TEXT NOT NULL
);

-- ── Товары ──────────────────────────────────────────────────────────────────
CREATE TABLE products (
    id           INT PRIMARY KEY,
    name         TEXT          NOT NULL,
    category_id  INT           NOT NULL REFERENCES categories(id),
    price        NUMERIC(10,2) NOT NULL,   -- цена за единицу, ₽
    in_stock     INT           NOT NULL    -- остаток на складе, шт.
);

-- ── Клиенты ─────────────────────────────────────────────────────────────────
CREATE TABLE customers (
    id           INT  PRIMARY KEY,
    name         TEXT NOT NULL,
    city         TEXT NOT NULL,
    registered   DATE NOT NULL
);

-- ── Заказы (каждая строка — один товар в заказе клиента, как в прошлых лабах) ─
-- Выручка строки = products.price * orders.quantity. Именно её мы будем
-- суммировать в CTE по клиентам, категориям и городам.
CREATE TABLE orders (
    id           INT  PRIMARY KEY,
    customer_id  INT  NOT NULL REFERENCES customers(id),
    product_id   INT  NOT NULL REFERENCES products(id),
    quantity     INT  NOT NULL,
    ordered_at   DATE NOT NULL
);

